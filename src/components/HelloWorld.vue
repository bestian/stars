    p 「我...我的真面目？」他突然意識到，他的蒙面布不知何時已消失得無影無蹤了。

    p 四周仍是不停躍動的黑暗。那裡面似乎藏有什麼。

    p 烏風覺得似乎有千百對忽明忽暗的眼睛，正向他注視。他有種被穿透了的感覺，十分難受。

    p 「你是誰？」那聲音問道。

    p 「我是誰？」烏風疑惑地問道。

    p 「你是黑暗的子民，來自地底的幽影。」

    p 「不，我出生在諾薩里亞的貧民窟。」

    p 「你的父母是誰？」

    p 「我...我不知道。」

