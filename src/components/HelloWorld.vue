    p 洛南紀五十九年十月十九日深夜，夢境

    p 「這是哪裡？」烏風問道。

    p 黑暗從四方八方包圍著他，向他張牙舞爪。

    p 「這是哪裡？」一個悠然飄臨的聲音，不知從哪裡傳來，重覆了他的問題。

    p 「你是誰？」烏風道。聲音中帶有強烈的敵意。

    p 「你又是誰？」那聲音迴盪著，反問道。

    p 「我是烏風。」

    p 「烏風是誰？」那聲音柔和地問道。

    p 「曾經是殺手，現在是浪人。」

    p 「你為什麼離去？」

    p 「我...為什麼離去？」烏風疑惑地問道。

    p 「你為什麼帶走割喉刀？」

    p 「我...為什麼帶走割喉刀？」

