    p 「我...是人嗎？」烏風似乎自己也不清楚。

    p 「你不是人類，而是黑暗的子民。你的故鄉不在人類世界，而在西南山脈的地底。」那聲音友善地說道。

    p 「我不是人類，那我是什麼？」

