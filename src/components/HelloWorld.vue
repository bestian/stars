    h2 尾聲

    p 故事的發展就像一串項鍊，開頭、結尾和重要的插曲組成了一個故事。而串起每一顆珍珠的那條線，卻是看不清楚，也不那麼重要的。

    p 稱為「諸星之戰」的那個年代裡，太多太多的英雄事蹟、冒險傳奇，實在難以道盡。每個地域都有自己說不完的故事，也有許多可歌可泣的英雄。不過，為了不要讓單獨個人掩蓋巨大的時代之輪，讓我們暫時回到更遼闊的視角吧。

    p 在戰亂剛平息不久的年代裡，商人由於龐大的財富，擁有了相當的地位。不過，由於產業仍是以農業為主，封建制度底下的領主和騎士們，仍是上流社會的主幹。

    p 貴族多半是早年隨先皇打天下的將軍、騎士、參謀的後裔，也有靠金錢買來爵位的商人。這類商人的領地通常在帝國外圍，含有可以致富的產物。比方說冰刃山脈的銀礦、西南山脈的銅礦和錫礦、南衛山脈的金礦和鐵礦等等。

    p 商人們的領地往往只包含礦坑和外圍一點點的土地，這對他們已完全足夠。朝庭的人也樂於見到有人能幫他們開礦，讓他們課稅。

    p 至於將領們的子孫，則是極度地參差不齊。

    p 有的人保持了父輩崇尚武德的傳統，成為才德兼備、文武雙全的騎士；也有的人因為沒有經歷過戰亂而過著奢靡、沉淪的生活；有的向商人學習致富術，並把封邑當成財產來經營賺錢；也有的如基斯坦卡候爵般，以無道的暴政作威作福，敗壞了父輩的一世英名，辱沒了曾經光榮的姓氏。

    p 統治南北沃土的諾魯夫王，也就是沃土公爵，是皇帝陸下的親弟弟，正值壯年，是深得民心的領主。他與北地領主米爾王，被稱為帝國末日時期的中流砥柱。

    p 在南沃土被盜賊佔據的日子裡，諾魯夫本來可以將他們掃平。但他認為罪不在平民，於是更加用心整治北沃土，讓盜賊們有回歸田畝的機會。只可惜他的德政，也敵不過連年的天災。

    p 貴族如此，平民的日子就悲慘得多了。在這動盪的年代中，他們不是被蝗害和疫病擊倒，就是死於苛政和刑具之下。再也沒有哪塊土地是永遠安全的，也沒有哪條命是能得享天年的。

    p 農民一離開土地，就像落葉脫離樹木一般，隨風飄盪，什麼事都能幹，什麼事都願意去幹。有的加入教會，只為了混一口飯吃；有的加入探險的行列，去西方作生與死的豪賭；有的揭竿起義，拚著身首分離，也要活下去。

    p 在人命最賤的時代，沒有人還能分得了是非對錯。為了生存，無論幹下多麼傷天害理的惡事，也能被諒解。他們連悔罪的時間都沒有，又得為下一餐的著落打算。

    p 因此，任何一個教會都能籍機擴展勢力。善神的修士幫人治病療傷；邪神的信徒則以死後的業報讓人懼怕；宿命之神的修士教人等待的藝術，因為苦難的歲月並非恆常。

