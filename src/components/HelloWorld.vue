    p 青銅製的刀面上刻著許多符文，刀柄下的環上綁著一撮長長的髮辮。

    p 「嗜血割喉刀！」芬斯特藍驚道，「那不是月影的鎮幫之寶嗎？怎麼會在你手上？」

    p 「我是一名殺手，也是竊賊，」烏風道，「月影從沒栽過這麼大的跟頭，栽在自己的成員上。」說完將刀收回。

    p 「但是，你為什麼要甘冒大險，偷取這柄割喉刀、背叛勢力龐大的月影公會呢？」芬斯特藍問道，「以你的身手，在裡面應該很有發展吧？」

