    h1 側記

    p 故事的原味，總比側記好看，不過側記並非為了好看而寫。而是作者本身試圖用更直白的方式，來說自己的故事。

    p 一開始寫《諸星之戰》時，我還是中學生。

    p 當時在迷紙上角色扮演遊戲(Tabletop Role-Playing Game, TRPG)。有一整學期，每週六和同學在家裡跑團，而諸星之戰的設定，像諾薩里亞、風谷、愛森、也就在遊戲中邊跑邊成形。

    p 原本寫諸星之戰時，想要拿跑團的故事當主軸，來延伸。但是，一開始隕星之夜的芬斯特藍，就不是遊戲中的人物，我想也是一個預兆。告訴我小說有她的生命，不是跑團記那麼單純。

    p 故事的發展和當時的遊戲，差異很大。角色的性格也頗有不同。不過，雖然有一些新的法術和彈性的戰鬥場景，多少還是看得出一些龍與地下城(AD&D)設定的斧鑿痕跡，我想這是我的小說功力還不夠吧。

    p 另外，從中學開始寫的好處是，隨著我自己的年紀增長，故事中人物的性格也會愈加鮮明而成熟。在接下來幾章會看得出來。

    p 至於在奇幻小說流行的時代，為什麼還要多寫一部呢？
    p 其實，有吳承恩的《西遊記》、托爾金的《魔戒》、麥克安迪的《說不完的故事》、娥蘇拉魏恩的《地海巫師》，真的不需要多一部奇幻小說了。
    p 只是，我有幾個問題，想要在諸星之戰中，和讀者一起思考。

    p 其中之一是：怪物為什麼該死？

    p 「怪物為什麼該死」困擾了我很久。電腦遊戲和一些小說中，怪物該死是不需要理由的。但是，現實世界中，哪有什麼人形生物，該死是不需要理由的呢？
    p 怪物是什麼？怪物不就是那些和我們無法溝通的人形生物嗎？就像西班牙人遇到美洲人，英國人遇到非州人，中國人遇到南島人那樣。
    p 當視他為「物」，就比視他為「怪人」更糟了。
    p 諸星之戰的第一章，敵人都是人，或是人被死靈術變成的活屍，沒有怪物。第一章唯一的怪物，反而是主角之一的烏風。
    p 怪物一定該死嗎？或許我們可以再想想何謂怪物。

