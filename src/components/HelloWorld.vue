    h3 1.1.1. 混亂的年代

    p 在人們開始流行吃蝸牛起，混亂的年代就開始了。

    p 洛南王結束了群雄割據的戰國時代，但在一統天下之後，僅維持了半個世紀的興盛，便因大帝駕崩、二世昏庸而陷入危機。

    p 接著，是災禍的十年。洪旱交迭、蝗害四起、瘟疫流竄、盜賊猖獗。許多有志之士因而西遷，到未知的山脈與森林開拓新的樂園。這些人被稱做「探險家」或「冒險者」。

    p 典型的探險家之路以皇都諾薩里亞為起點，一路向西沿伸，直到帝國的西邑口--落雲城，再跨越平西橋來到西方，之後折而向南，到達國境西南的沃土平原。

    p 沃土平原，是盜賊出沒的危險地帶。旅人若沒有兩把刷子，來此就等於是送死。

    p 其實，沃土一度是富饒的農田，但在多次欠收後，民不聊生，加上地處邊陲、鞭長莫及，好幾股盜賊勢力逐漸成形。他們像飛蝗般襲捲一個接一個的村落，成為農民們除了天災、蝗害和瘟疫外，第四個夢魘。

    p 當然，洛南王不會坐視沃土淪為賊窟，因此在四十六到五十一年間，三度派遣西境領主諾魯夫親王至沃土平亂。諾魯夫王的騎隊驍勇善戰，但盜賊們行蹤飄忽，難以聚而殲之。親王的名聲雖因西征傳開，盜賊問題仍未徹底解決。

    p 諾魯夫王幾番西征，雖未徹底平亂，卻給沃土帶來生機。農人學習到，沒有什麼比自己的雙手更可靠。與其坐等英雄，不如化犁為劍，習農尚武，靠自己的力量，守護自己的家園。村民們擴大聚落並修築圓型土牆，稱之為「站」。愈來愈多人，在農事的空擋，接受軍事訓練，成為非正規的士兵。女人與小孩雖不宜衝鋒陷陣，也都加入了自立習武的行列。

    p 沃土雖然危險，仍吸引了不少旅人，自王國各處前來。除了探險家外，還有考古隊、商販以及職業傭兵。

    p 考古隊的興盛吸引了許多珠寶商人。如果他們僱的考古學家夠專業、運氣又好，便能一夕致富--價值連城的飾品屢見不鮮。曾經出土過幾批精雕的紅寶石項鍊及翠玉護符，其工藝超越了王國中最專業的匠人。

    p 究竟是誰製造了這些精品？

    p 考古家一致同意，在沒有歷史能顯示其長度的黑暗紀元前，曾經出現過一個文明。可惜它沒有留下什麼記錄，除了殘破不全的石板、器物，刻著難以解讀的文字。

    p 但是，這個失落的文明究竟有多進步呢？

    p 一派的專家相信他們擁有高於現代的文明，並以雕刻寶石的技術為佐證；另一派卻認為古文明在許多方面都是較為落後的，並以青銅器、陶器古物做為佐證。那時的人似乎還不懂得怎麼鍛鐵。

    p 這些學者的爭論對商人和保鑣無甚意義，卻讓他們接到一批又一批的生意。這也為盜賊帶來更多的獵物，間接也助長了他們的勢力。

    p 沃土的繁榮和危險一併成長，使它成為孕育西行探險隊的搖籃。

    p 洛南紀五十四年，一組探險家在沃土西方，西南山脈的山腰上找到一片土質肥沃、雨量充足、背山面河的平地。於是拓荒者、貧民、獵戶和民兵們接踵而至，在新的樂園伐木、建屋、開闢耕地。人們將新的村莊取名為「盛恩村」，以感謝天神的恩賜。

    p 首先發現這片土地的探險隊長之名，銘刻於村子中央的石碑上。後到村子的人，常會好奇地尋問「魔術師阿布金」是何等人物。這位阿布金也在幾年冒險後，決定在村中定居，用他的技藝，帶來歡笑與安全。

    p 在盛恩村安穩之後，考古家、商人和保鑣們的足跡也跟上了西行小徑。他們在盛恩村周圍架設考古場，也如預期地得到他們想要的：古物、遺跡、珠寶。當他們返回國境、大加渲染後，自然吸引了更多的西行人潮。

    p 王都附近的吟遊詩人有的為中土的沒落感到可悲，有的則歌頌探險的行為。曾經跟隨探險隊深入密林的詩人芬斯特藍，為探險家作了一些歌曲，其中一首深受他們喜愛，在西南山脈的丘壑間廣為流傳。內容如下：

    p ***
    p 探險家之歌 芬斯特藍．薩斯坦提斯
    p 我們離家去流浪，為了尋找新天堂
    p 在西南山脈的最深處，有著黃金一籮筐
    p 大地富饒如沃土，野獸群聚如牛羊
    p 為了無限天地待開拓，我們離開了故鄉
    p 為了故鄉再不見麥黃，我們離家去流浪
    p ***

    p 歌中的「黃金」原本只是比喻，卻在盛恩村西北的山區中應驗了。

    p 探險家在尋找新的水源時，意外發現了河中的沙金。人們在該處建了一個新的村莊，名為閃金村。

    p 兩三年的時間，沙金便被淘得一乾二淨，閃金村也因而閃耀了起來。在沙金淘盡後，人們轉而發展務實的林業與狩獵，維持了村中的繁榮。

    p 村子另一個繁榮的原因，就是頻頻來訪的考古學者，為了追求更多的古物，自王國各處前來。

    p 奇怪的是，這些學者愈來愈不願意發表他們的成果，有的還僱傭兵將整個考古場守住，像是看守一座要塞一般。

    p 洛南紀五十九年初，國境中發生了一場遠甚於往常的大瘟疫，患者膚生紫斑，高燒虛弱，不出三日而死。最嚴重的疫區正是沃土。

    p 更多的難民逃往西南山脈，而沃土因為人丁減少、農事荒廢，逐漸淪為徹底的賊窟。
    p 正當沃土的繁華邁向黃昏，一場新的黎明，也許是新的黑夜，即將來臨。

    h3 1.1.2. 芬斯特藍
    p 洛南紀五十九年十月十九日，王都諾薩里亞

    p 「欸！芬斯特藍！你聽說那個預言了嗎？」一個穿著講究、面目清秀的年輕人說道。

    p 「什麼預言？」芬斯特藍躺在藝文廣場的草地上，看著初冬午後清朗的天空，隨口應著。

    p 芬斯特藍，二十一歲，薩斯坦提斯男爵的長子。他有著金色垂肩的長髮、一雙出外探險練出的結實的臂膀、優雅迷人的吟唱聲調。

    p 他有過不少浪漫故事，卻從不提及婚姻。有人猜測，雖然到了適婚年齡，芬斯特藍．薩斯坦提斯仍然嚮往冒險。

    p 「那個皇家森林裡的隱者講的，」芬斯特藍身旁的年輕人續道，「你也知道，他一年難得開一句金口，上禮拜啊，一說就是個可怕的預言欸。」

    p 「哦，所以呢？」芬斯特藍不太在意，繼續享受千草輕扎的感覺。

    p 「所以？」年輕人加大音量，「所以今晚就特別重要啦！陛下已經下令，如果隱者的預言落空，他在皇家森林的茅屋就要拆除！你知道很多人都討厭那個神秘兮兮的隱者。」

    p 「我看要趕他走也沒那麼容易，畢竟他是萬物和諧宗的最高隱者。等等，他到底預言了什麼？」

    p 萬物和諧宗，並不是一個真正的教派，是吟遊詩人自己為隱者取的。隱者們崇尚自然，過者非常簡樸的生活，有的隱者甚至不和他人同住，像皇家隱者就是獨居。

    p 「他說，十九日，也就是今天的晚上，天上的星辰會墜落，並把災禍帶到人間。星辰隕落欸！你想有可能嗎？」

    p 「套句探險家的話，只要心還在跳，什麼事都是可能的。」芬斯特藍笑道，「那好，咱們就約黃昏在西牆上碰頭，一起看看那預言會不會成真吧。」

    p 「怎麼，你待會兒有事？」

    p 「是的，瑞福，」芬斯特藍坐直了身子，嚴肅地說道：「我要去一趟古董店。最近那兒來了奇怪的人，可能是考古學家，買了許多不相干的東西，處處透著詭秘。而且，他說他今天還會再去。」

    p 「你怎麼知道這消息？」年輕人瑞福問道。

    p 「這些都是老闆告訴我的。」芬斯特藍笑道。

    p 「你跟老闆熟？」

    p 「喂，別忘了我也到過西方冒險。我們以前是同伴啊。」

    p 「冒險？我們的公子哥兒芬斯特藍能冒險？真好笑！你想必都是在僱保鑣和傭兵替你作戰吧？」瑞福說著捧腹大笑。

    p 「是啊，是啊，隨你怎麼說。不過我真的得走了。」詩人說著起身。

    p 「那就黃昏見啦，我可以趁這時候去側街東南角哪兒逛逛...」

    p 「喂，你……」芬斯特藍半認真地調侃道。

    p 「喂，我沒擋你探險，你也別擋我『探險』。哼，你自命清高，看來更有可能一輩子當光棍呢。」瑞福做了個鬼臉，跑掉了。

    p 「這就是王都的『年輕人』，唉，」芬斯特藍苦笑著搖搖頭，「我還是比較喜歡山上的探險家。」

    h3 1.1.3. 神秘的鏡子
    p 吟遊詩人收拾散亂的樂譜，將短琴在腰帶上固定好，走向西側街上。

    p 年輕小販向他兜售幸運石，他禮貌地拒絕了。

    p 快到目的地時，芬斯特藍注意到有個披灰色斗蓬的人，鬼鬼祟祟地躲在街角的暗巷，不知在做什麼。

    p 他悄悄走近，沒讓對方發現。

    p 那人左手拿著一只沒有鏡面的青銅鏡。它只有把手，連著一個中空的環。

    p 灰袍人伸出右手，拿出一顆珍珠。

    p 大概值百枚碎金呢，芬斯特藍心道，這人穿著簡陋，卻這麼有錢！

    p 那人將珍珠緩緩移向環中央空虛的鏡面，唸唸有詞：「麗兒．施拉．康．薩．維．塔」

    p 「麗兒．施拉．康．薩．維．塔」芬斯特藍在心中默唸。

    p 奇妙的事情發生了。

    p 珍珠憑空溶解成一片淡淡的白光，形成一層薄膜的鏡片，而後漸漸暗掉，消失。

    p 芬斯特藍心想，果然不出所料，是「魔法」--愛森時代特有的力量。他曾在古老的樂譜上找到類似的神秘力量，但十分模糊、陌生。

    p 芬斯特藍稍微走遠，等那人走向古董店時，他便跟了過去。

    h3 1.1.4. 古董店
    p 古董店掛著木製的招牌，上面用金色的字刻著：「塵塵屋」

    p 那人稍微看了一下招牌，四下張望了一會兒，便走了進去。

    p 芬斯特藍隨即跟進，並投給老闆一個眼色，老闆點了點頭，表示會注意。

    p 古董店老闆是芬斯特藍的朋友，高瘦、精明，名叫維卡克。因為他留著長鬍子，又叫做長鬍子維卡克。

    p 長鬍子維卡克說道：「歡迎歡迎！兩位客人請請請，本店古物飾品應有盡有，每樣都是西南出土的真品，挑看看吧！」

    p 那人拿出神秘的銅鏡，走向陳列架，邊說：「讓我看看...」

    p 芬斯特藍假裝欣賞古董，一邊留意那人的動作。

    p 灰袍人將青銅鏡拿在眼神，飛快地掃視眾多古物。

    p 那人選了一些，再用鏡子仔細審視，從中挑出幾樣他中意的古董，拿到櫃台。

    p 他選了一枚方形圓角的護身符、一把青銅短劍、兩個略有破損的青銅碑。這些都不是什麼珍奇的寶物。

    p 「啊，客人，您真有眼光，」長鬍子維卡克假意說道，「上週來的鑑賞家也說這些都是千年以上的古董，又完整，又精緻，所以...價值比標價寫得高出不少。這陣子太忙，沒來得及寫新的標價，呵呵，請別見怪。」

    p 「什麼鑑定家？」那人懷疑地問，「長什麼樣子的？」

    p 說著，兩眼透出奇異的光芒。價碼對他來說反而不那麼重要了。

    p 「嗯，就是...」維卡克盡量維持鎮定，想要從認識的鑑賞家中，挑一個不會找麻煩來講，卻一時想不出來，「你知道的，就是那個...」

    p 芬斯特藍急忙上前解圍：「呵呵，正是區區在下。」

    p 「你又是誰？」那人並沒有注意到身後有人一起進來，愣了一下。他緩緩地，用略帶懷疑和輕蔑的眼神打量著芬斯特藍。

    p 芬斯特藍覺得從帽尖的羽毛到鞋上的汙泥都被仔細端詳，讓他感到渾身不自在。

    p 不過，他還是維持了外表的鎮定：「我叫莫魯法，曾經帶隊到沃土的考古場。」

    p 「莫魯法先生，幸會，幸會，」那人話鋒一轉，問道，「你對於哪個時期最有研究？」

    p 「愛森後葉，大災變來臨前的煇煌的年代，精工飾品及城堡遺跡不同凡響。那真是個令人嚮往的國度！」芬斯特藍憑自己的知識答道。

    p 「喔？所以你相信古文明曾一度高於現代？真是有趣！」

    p 「難道不是嗎？」

    p 「算了算了，這場爭論是不會有結果的。一切都要靠新的發掘來佐證。」那人轉向老闆，「我們還是先談生意吧。你說價錢不是上面寫的，那是多少？」

    p 「其實，我對那些東西也挺有興趣的。」芬斯特藍說道。剛才巷子裡的景象已讓他下定決心，不能讓這個會魔法的人得到他想要的。

    p 「那麼，既然你們都想要，那麼定價也沒意義了，得要兢標啦，」老闆摸了摸他的長鬍子，笑道，「我可就拜兩位之賜，要發財啦。咱們一樣一樣慢慢來，起價五十碎金，嗯...先從青銅劍開始好了。」

    p 「我出六十。」那人說。

    p 「七十。」芬斯特藍毫不客氣。

    p 「一百。」

    p 「一百五。」

    p 「兩百。」

    p 「三百。」芬斯特藍故意顯得荷包滿滿，一次跳一百。其實，此刻的兢標，對他來說再容易不過了--他從小就在各種商店打轉，而且身上一整疊諾薩里亞商業公會的金票，顯然不會不足。

    p 芬斯特藍．薩斯坦提斯並不喜歡揮霍，但在需要時，財富的確能夠發揮重要功能。
    p 「...」那人果然沒有再加價。

    p 「那麼青銅劍由芬斯特藍得標！」老闆大聲宣佈。

    p 芬斯特藍使給老闆一個眼神，但已經太遲了。

    p 「芬斯特藍？」那人又驚又怒，「好啊，原來是大名鼎鼎的貴族，詩人探險家！什麼莫魯法，全是騙人的！你們串通好來坑老子的錢，利用老子的寶鏡！老子不玩了！」

    p 他氣憤地揮舞著拳頭，吼道：「長鬍子的混蛋！你會知道欺騙顧客的下場，你將無法從西南山脈買到任何貨物！」說完便氣沖沖地走向門口。

    p 芬斯特藍過去拍了一下那人的肩膀，一面道歉：「抱歉抱歉，我真的...」

    p 那人一把將他甩開，大步走了出去。

    p 芬斯特藍轉頭，走向沮喪的老闆。

    p 「抱歉給你惹了個大麻煩，老友，」芬斯特藍難過地說道，「但狀況也沒那麼糟嘛，那人頂多也只能影響一小區的考古場...對了，我最近會去西方一趟，到時候一定會帶些好東西回來給你，做為補償。這幾樣古物，我就跟你買下了。」

    p 說著，他取出一小疊金票，每張都是面值百元的大鈔。這樣一疊金票，遠超過維卡克一年的收入。

    p 「這不成，這不成，」維卡克道捻著他的長鬍子，說道，「沒這麼貴的道理。」

    p 「絕對有，可能還不止呢。」芬斯特藍誠肯地說道，相信我，「那個怪人的鑑定不會有錯。」

    p 「那我就恭敬不如從命了，」老闆苦笑道，「其實你也不必特地西行，我還不至於被那人嚇到。而且，那兒不是正流行瘟疫嗎？」

    p 瘟疫是讓人聞風喪膽的恐怖。人一但染上，若無法醫治，所有親朋好友都將離他而去，留下他一個人等死。就算好運活了下來，還要面對痊癒之後的孤寂。

    p 「對，瘟疫，」芬斯特藍道，「不過我打算西行，倒不只是為了蒐集古物。」

    p 「你還想冒險？」維卡克笑道，「你也快要不年輕了，芬茲，該找點正經事做做了吧？」

    p 「我可不覺得冒險有什麼不正經。我靠著我的琴、歌聲、文筆和劍，也不至於餓著了。」

    p 「但是，你父親總不會喜歡看你這樣吧？」

    p 「他啊...我已經答應他，如果二十四歲前還一事無成，我就去管理莊園、結婚、幫家裡掙點名望什麼的。」

    p 「哈哈哈，說不定真有那麼一天...芬茲談生意、結婚啊...說不定喔...哈哈...」維卡克撚著他的長鬍子，大笑。

    p 「再說吧，我得走了。」芬斯特藍說道，「希望還能再見到你，老友。」

    p 「你會回來的，芬茲，我很清楚。」維卡克點了點頭，「你是個出色的冒險者。」

    p 「不過，現在跟西方某個考古隊結了仇，就有點麻煩了。」芬斯特藍道，「聽說西方那些考古隊愈來愈橫蠻，手筆也愈來愈大，傭兵一僱就是七、八十個。」

    p 「無論如何，還是祝你好運。」老闆說著用麻布將古物一件件包好，交給芬斯特藍。

    h3 1.1.5. 隱藏的魔法
    p 向長鬍子維卡克道別後，芬斯特藍看了一下天色，十分晴朗，離黃昏大約不少時間。

    p 於是，他到珠寶店買了八顆珍珠，再回到藝文廣場，找個不起眼的角落坐下。

    p 他拿出青銅鏡，那是他剛才趁那人被怒氣沖昏頭時，用扒竊手法弄來的。

    p 芬斯特藍從珠寶袋中取出一顆剛買的珍珠，彷照那怪人的方法，移近青銅鏡。

    p 鏡框上浮現了一些銀白色的字句，是以古愛森文寫成的。芬斯特藍憑著對愛森文的一些知識，加上剛才的記憶，唸出了咒語：「麗兒．施拉．康．薩．維．塔」

    p 如他所預期的，珍珠化為白光，透鏡也發生了變化。

    p 他拿出古物，用透鏡一一端詳。

    p 透過魔法透鏡，芬斯特藍看到護身符上有一圈淡藍色的光暈，裡面還飄著紅棕色的光點，彷彿在熟悉的世界背後，透出的神秘色彩。

    p 對芬斯特藍來說，萬事萬物都不再單純了--他清楚地看到魔法，潛藏在平時肉眼不能及處，卻帶著不知多麼強大的力量；它附著於這只護符上，歷千百年蒼桑而不衰...

    p 想到這裡，芬斯特藍驚覺到，這還只是一小部份。在遙遠的西南山脈，一批又一批附有魔法的古物正在出土，若是它們落入野心家手裡...不，強大的力量本身就會使人墮落，淪為野心家或暴君，這樣的例子史上屢見不鮮...

    p 但是，芬斯特藍想到，我自己也會使用一點點從古樂譜發現的咒語，是不是我已漸漸走上野心家之路？像剛才隨便扒竊的行為，不是我平常會做的...

    p 芬斯特藍打住思緒，繼續觀察其他的古物。

    p 青銅短劍上附有飄著暗藍色光點的淡藍光暈，而古文碑卻沒有顯現什麼特別的光芒。

    p 他將東西收好，看了看天色，離約定的時間還有段距離。於是他走向皇家樹林，神秘隱者的茅屋。

    p 是真的嗎？他想。星辰...在傳說中，星界是神明的居所；有些歌謠還把繁星都當作神明...

    p 隕星？聞所未聞。可是隱者身為皇家樹林的守護者，平日一語不發，現在卻說了這樣的預言...或許有更多未知的事，能從他那兒問到...

    p 想著想著，芬斯特藍踏進皇家森林的小徑。

    h2 1.2. 災星隕落
    h3 1.2.1. 隱者的預言

    p 皇家森林，一個與世隔絕的所在。
    p 芬斯特藍叫不出林中百種植物的名字，但能感受到一股神聖而安寧的氛圍，是此地特有的。
    p 點點陽光自葉隙透下，百花在蟲鳴鳥語中綻放。
    p 一隻松鼠從樹梢探出頭來，隨即羞怯地躲入森林深處。一對蝴蝶追逐著飛過芬斯特藍身邊，消失於一叢灌木之後。
    p 林中小徑並非由石板榙成，而像森林故意讓出的空隙，曲折而寬敞。花朵如路標一般，長在兩旁，讓人不至迷失方向。芬斯特藍不知不覺腳步就放慢了。

    p 走著走著，不知過了多久，一片空地突然轉進視線。

    p 草地上立著一座簡單樸素的小茅屋，屋旁有個水井，更遠處有塊大石，上面坐著一位老者。

    p 老者有著銀灰而蜷曲的垂肩長髮，皺紋滿佈的面孔和藹而安詳，似乎帶著微微的笑意，又像附著淡淡的悲傷。

    p 他閤著眼盤坐石上，宛如另一塊大石，又像一株巨樹，和整片森林溶為一體。

    p 芬斯特藍花了一些力氣，才讓自己開口，打破寧靜。

    p 他道：「睿智的隱者，森林的法皇啊，您預言星斗的隕落，引起了人們的恐慌。您的言語過於玄妙，能否為我稍加解說？我乃歌頌傳奇的詩人，名叫芬斯特藍．薩斯坦提斯。」

    p 隱者沒有回答。

    p 芬斯特藍續道：「自然的使者，安寧的導師啊，神秘的事物層出不窮，我需要您的智慧來釐清心中困惑。竟究魔法是何等事物，又為何它在此時現身？」

    p 隱者毫無反應。

    p 芬斯特藍想了半晌，改變策略。

    p 他拿出短琴，彈起一首《怒海》，弦律中帶著驚濤裂岸的力道，又有被迫退回海中的無耐。既然來到這裡，就沒那麼容易折回，芬斯特藍心道。

    p 彈到一半，隱者總算有了反應。他隨著節奏，用緩慢和柔和的聲音唱出詩句般的預言：
    p 「遙遠國度傳來落葉恐懼，
    p 西行旅程只因不可自欺；
    p 黑夜與白晝交替的戰場，
    p 開場與結局將永不分離。」

    p 那聲音帶著深深的同情，似乎在哀悼什麼。沒有一絲急促，沒有一絲嚴厲。

    p 芬斯特藍手上不停，接著唱道：
    p 「星斗的隕落有何深意？
    p 神明的憤怒如何平息？
    p 王國的西方已有變故，
    p 魔法的力量是何奧秘？」

    p 隱者接唱道：
    p 「什麼事物能撼動大地？
    p 什麼災難能將人類抹去？
    p 什麼力量能保存靈魂？
    p 什麼力量能將亡者喚醒？」

    p 芬斯特藍想了半晌，只覺得不安，但悟不出隱者清楚的意思。於是他唱道：
    p 「智者之語透出真理，
    p 癡愚的我無能會意；
    p 微風的守護者，請告訴我，
    p 我該往何處去？」

    p 隱者答道：
    p 「黑暗的道路將在今夜升起，
    p 死亡的花朵將盛接雨滴；
    p 崇尚槍騎的神殿中，
    p 浮出的奧秘將表露無疑。」

    p 芬斯特藍聽懂了指引，便靜靜彈完《怒海》，再拿出羊皮紙和羽毛筆，抄下了隱者所說的每一個字。

    p 結束後，他看到隱者似乎又回到了定靜的狀態，便不再多言。向隱者深深一揖，離去。

    p 他踏著飛快的腳步，不再留戀小徑的和諧，一路走出了森林，朝向槍騎神殿。

    h3 1.2.2. 槍騎神殿

    p 槍騎神，九主神之一，受著王國各地人們的崇拜，也有著不同的名字。

    p 修士稱祂「自由之神」、南方風谷的人稱祂「風神」，北方人稱祂「文明之父」。由於諾魯夫親王的「聖槍團」聲名卓著，人們主要稱祂為「槍騎神」。

    p 槍騎神的月祭是在每月第三日，通常伴隨著騎士競技和投槍比賽，場面之壯觀，與劍神祭中的擊劍和弓神祭的射箭比賽不相上當。

    p 槍騎神的歲祭是在每年二月的春分時節，卻是因風谷的習俗而訂。

    p 風谷終年強風，冬季颳西風、夏季颳東風。每當春分風向改變時，就會有大片烏雲撲天蓋地自東海而來，當地人稱之為「雲被」，堪稱一大奇景。

    p 在神話故事中，槍騎神名叫那沙莫提。祂好酒、勇敢、隨性、熱心，最厭惡任何形式的「囚禁」。沃土一帶農民不使用籠子和圈欄來關家禽家畜，跟信仰槍騎神有關。

    p 在王都諾薩里亞，槍騎神也備受祟敬。鎗騎神的修士不僅懂得醫療、驅邪，也擅騎馬剌鎗，能在戰時保衛王城。

    p 芬斯特藍踏著石板路，走到槍騎神殿的門口。

    p 神殿並不很高，但十分寬廣，沒有門板的門上刻著一把鍍金的長槍和一行金字：「放下成見」

    p 進門之後，只見光線從兩側的玻璃彩窗透入，地上的紅地氈直伸至高起的聖壇。

    p 聖壇左右是七枝蠟燭的燭檯，但都只點了間隔的三枝，象徵創造的留白。

    p 再後面是一個下凹的大理石高檯，裡面湧著神聖的活水。

    p 神殿修士被著天藍色的長袍，坐在大廳一長凳子上，手裡拿著一本暗紅色皮的《生死書》，那是進階修士必讀的知識。

    p 芬斯特藍知道這位修士是顯赫貴族的後裔，曾參加過落雲城之役，立有戰功，但不知道他的名字。

    p 「可敬的修士，我是芬斯特藍，想向您請教一些問題。」芬斯特藍說道。

    p 「當然，」修士笑道，「我親愛的兄弟，你可以提出任何的問題--知識是共享的。」說著緩緩起身，將手中的書闔上。

    p 「那麼，因為時間緊迫，我便直接問了：星斗隕落究竟有何意義？」
    p 「嗯...」修士沉吟了一會兒，縐眉道，「我不很清楚，真的...我只能說，這是一個災難的預兆，預示了一個災難...或許是個空前絕後的大災變。」

    p 「是天災嗎？還是戰爭？瘟疫？或是...」

    p 「我不清楚，」修士嚴肅地說道，「不過，有可能是它們全部。」
    p 兩人陷入了一陣沉默。

    p 芬斯特藍又問道：「那麼，魔法究竟是什麼？它和神賜的力量有何不同？」

    p 「魔法啊...這種技藝已經從世上消失很久，很久了...在古老的記載中，魔法是來自對元素的掌握。人們利用手勢、聲音和法材，將地、水、火、風等元素組合成複雜的形式，便是所謂的魔法。

    p 「相對於魔法，神賜力量的本質是明確的。魔法非常不穩定，也不具良善的本質。

    p 「純粹的力量是極度危險的東西，我的兄弟。當邪神們向世人降下災禍時，魔法一度被當做救贖之道，最終還是消失於世間。多半是因為無節制的強大力量，所造成的反噬吧。」

    p 「不瞞您說，」芬斯特藍道，「古代的魔法正不斷地被發掘出來。這些渴望力量的竊賊，正是以考古活動偽裝的學者。今天我看到了魔法的力量，發現它可以儲藏於器物之中，不知有什麼可怕的效果。」

     p 「那麼，兄弟，遵循心的引導，到西方去一探究竟吧。你會碰到許多危險，但你也會因而成長。

    p 「不要排斥魔法。雖然神職人員厭惡它，但你不需要背負我們的成見。力量可以用來做很多事，無論善惡；但若你沒有力量，則將一事無成。」

    p 「可敬的修士，我今天聽到這樣一則預言，它說：

    p 『什麼事物能撼動大地？
    p 什麼災難能將人類抹去？
    p 什麼力量能保存靈魂？
    p 什麼力量能將亡者喚醒？』

    p 「請問，這是何意？」

    p 修士想了一想，答道：「只有神的力量能撼動大地，只有天降的災難能將人類抹去...幽冥的力量能保存靈魂...邪神的力量能將亡者喚醒...善神也可以...不過還是透過幽冥...這樣吧，既然你即將踏上西行的旅程，步入廢墟充斥的大地，我就告訴你一些活屍的知識，或許會對你有益。」

    p 「活屍？像是僵屍一類的怪物？」

    p 「沒那麼單純...」修士認真地說道，「當幽冥的力量侵入亡者的身驅，使之墮入半生不死的狀態而產生的存在，就稱為『活屍』。我說『存在』而非『生物』是因為它們其實已經失去生命了。

    p 「活屍大都來自邪惡的儀式或詛咒，但不同的力量會造出強弱和形體懸殊的活屍。其種類一言難盡，而且可能會愈來愈多。不過，有些常見的、低階的活屍，比較可能會出沒於你即將踏上的道路。

    p 「兄弟啊，要知道，活屍們都已經死了，也喪失了一切的人性。『消滅』它們不但不是罪惡，反而是讓它們解脫。

    p 「注意了，兄弟，我將告訴你一些常見的活屍：

    p 「首先是以亡者骸骨，附以幽冥怨念製成的骷髏妖。它們如同所有的活屍一般，不畏寒冷，不會恐懼。它們力量不大，但空洞的身驅會使箭矢穿透。

    p 「再來是僵屍，由帶肉的屍體附以怨念製成。它們比骷髏妖更強壯，但比較遲緩。只要速度夠快，就可以擺脫它們。

    p 「再來是餓鬼，由邪惡的欲望、饑渴和怨念附於亡者而成。通常在亂葬岡出現，殺人食屍，又名食屍鬼。它們因永不滿足的饑渴扭曲了肉體，而擁有使人麻痺的利爪。被它們殺死的人會受到詛咒，成為它們的同類。

    p 「最後是影魔，有生之年我沒見過這種怪物。它們是從幽界喚來的存在，或是原本屬於一個人的影子，經由向主人報復的怨念產生的怪物。它們不具有牢固的形體，不受武器所傷，卻能透過冰冷的碰觸，吸取生人的力量。

    p 「神聖的修士可以超度活屍，或至少使它們退散；而魔法的力量，也能傷害被怨念強化的驅體，甚至像影魔一般的半幽界存在。

    p 「去吧，我的兄弟，不要被險阻給嚇倒。只要你以良心為準繩，槍騎神會保佑你的。」

    p 芬斯特藍心中一邊默唸活屍的特性，一邊震驚於它們的威力。

    p 凡人如何與邪惡的活屍對抗？消滅不會受傷的惡靈？

    p 中等的劍技、微弱的魔法、冒險者的直覺、不知何用的古物，他心道，這些就是我僅有的力量。

    p 「兄弟，你不用太擔心，」修士察覺到了芬斯特藍眼中的陰影，說道，「救世的道路不會永遠孤獨。你將會找到同伴。」

    p 芬斯特藍向修士道了謝，踏著沈重的腳步走向西牆。

    p 救世？他邊走邊想，不，這離我太遙遠了。

    p 我熟識的『世界』還太小，太小...不過，如果救世變得和救自己相同，那麼也只好以天下為己任啦。

    p 或許這就是所謂「時勢造英雄」吧...我會成為英雄嗎？英雄...
    p 想著想著，已循著石階上了城牆。

    h3 1.2.3. 星斗隕落

    p 城牆坐了上百人，散在各處，看來都是等著驗證隕星預言的。

    p 芬斯特藍憑著銳利的眼光，迅速地找到了朋友瑞福，發現他身邊還坐著一名女子，兩人正在談天。

    p 芬斯特藍走近，輕咳了一聲。

    p 「唉呀呀呀，你可終於來了，」瑞福笑道，「我和瓦林娜已經在這兒等了將近一個鐘頭。對對對，我來介紹你們兩個認識認識，這位是詩人芬斯特藍，這位是南方來的瓦林娜。」

    p 瓦林娜天真地看著芬斯特藍，發現他也正好在向她頷首微笑，突然羞怯地低下了頭。

    p 她看起來約莫十五六歲，留著一頭褐色及肩的短髮，臉型略尖，身材中等，看起來十分健康漂亮而清純，不像一般風塵女子的滄桑。

    p 最令芬斯特藍著迷的，是她那雙紅寶石般發亮的眼睛，以及其中透出的光芒，有著來自南方的生命力。

    p 「我...」瓦林娜用帶有口音的通用語道，「我叫瓦林娜，來自風谷。請多多指教。」

    p 「是這樣的，」瑞福接著解釋道，「我看她氣質不凡，所以花了一筆錢幫她贖身...她說她想去沃土，我想既然你要去，何不乾脆把她帶著？一方面讓她有個人照料，二方面你也不會寂寞啦。」

    p 寂寞？芬斯特藍心中暗自苦笑。身為冒險者，若不能習慣寂寞，怎麼忍受得了冒險生涯？有浮雲為伴、有飛鳥為友，又更有何求？不過...

    p 「可是，」芬斯特藍疑惑地問道，「她是誰？怎麼會想去這麼危險的地方？還有，你用自己的錢幫她贖身？這不像我認識的瑞福。」

    p 「我想去冒險。」瓦林娜堅定地說道。

    p 「冒險？！」芬斯特藍好奇地望著她，笑道，「小女孩，別把冒險當作兒戲，那是有生命危險的。且而，如果妳沒辦法保護自己，帶著妳對我來說會是一大負擔。妳能保護自己嗎？」

    p 「我可以保護自己的。我也不是小女孩，只是看起來年紀比較小，」瓦林娜不好意思地說道，「而且，別人不會想傷害我的，他們只想...呃，總之，我也會用武器，必要時...我也會殺人的。」

    p 「喔？」芬斯特藍覺得很有趣，「『殺人』可不是平常的字眼。好吧，看在妳能讓瑞福這麼反常的份上，我就帶妳同行，但是只到落雲城為止。另外，我今晚就得動身，妳可能沒什麼時間準備。還有，我必須再次強調，妳得負責保護自己...」

    p 「好。」瓦林娜簡單地回答。

    p 「對了，如果妳真的那麼厲害，也別輕易殺人，那會讓我們平添不少麻煩。」他半開玩笑地說道。

    p 「沒問題，」瓦林娜燦爛地笑道，「我會注意的。東西我也都帶在身上了，隨時要走都沒問題。」說完將淡綠斗蓬披上，拄起足邊的及身木杖，十足冒險者的架勢，只是顯得可愛有餘，威風不足。

    p 「好了，好了，」芬斯特藍笑道，「還不是時候，等星斗隕落了再說。」

    p 「所以說嘛，」瑞福笑道，「瓦林娜可不平凡哪。連芬茲都一反常態了。」

    p 芬斯特藍突然想到一項明顯的矛盾：她是被瑞福贖身的，但剛才瑞福和她只是並肩而坐。她的身世也不清楚：冒險者怎會成為青樓女子？或者，這根本是一種偽裝？

    p 帶著謎樣的人同行，平添了一層不確定。在冒險的生涯中，太多的不確定是致命的。

    p 沒錯，是致命的。

    p 芬斯特藍正要開口拒絕瓦林娜同行，突然一陣尖叫聲打斷了他。

    p 星斗隕落了！

    p 乍現的流星雨，帶走了龍心座的一顆燦星。

    p 此時，世界各地的人們都震驚於天空的異象。諾薩里亞皇家天文塔的最頂端，大星象師顫抖著寫下：「末日將臨」四個字。

    p 流星雨愈來愈大，愈來愈大，直到整片星空被刷得如白晝一般通明。

    p 飛瀑般的流星雨中，閃現了成千上萬的流星。原本高掛於天際的星斗中，有些竟也離奇地消失了，像被星雨沖下了天幕。

    p 在諾薩里亞皇家天文塔的最頂端，大星象師在驚駭中仔細地觀察流星雨的始末，並注意到一個異象：九主神的星座都各自隕落了一顆星，正好一顆。

    p 三善神，也就是秩序之神、慈悲之神、自由之神的星座，北極輪、龍心座、銳槍座中，分別隕落了名為凝光、長碧、煌鑫的副星。

    p 審判之神、痛惡之神、災禍之神這三位惡神的星座，也就是天枰座、盤蛇座、陰空座中，隕落了名為玉輝、琉璃、墨漾的副星。

    p 另外三位主神，介於善惡之間的宿命之神、平衡之神、混亂之神的星座，宿命輪、雙杖座、密網座中，也隕落了名為紫焰、穹銀、玓環的副星。

    p 大星象師記下了每一顆星的星座，以及隕落的時間。他不厭其煩地寫下各個主神的星座及隕星的名字，是有原因的：上述每一個名字的份量，都遠遠超過了洛南的帝王。

    p 在震驚於異象的同時，芬斯特藍已穿好他的護身皮甲，取出他的褐色斗蓬披上，並清點好隨身攜帶的物品，包括乾糧、火具、毛氈、小刀、短劍、輕十字弓、繩索等等；還有一些內袋中藏著的怪東西，像沙子之類的，是施法用的法材。

    p 瓦林娜偎到芬斯特藍旁邊，顫抖著握住他的手。芬斯特藍感覺到一隻冰冷而柔軟的手，知道她心中害怕，便握得緊了一些。

    p 牆上其他的人們，包括瑞福在內，都嚇得不能動彈。星空的異象，彷彿帶著魔力似地定往了所有人的目光。因此，當一個黑影悄悄地走上城牆時，誰也沒有留意。

    h3 1.2.4. 考古家的復仇

    p 黑影緩緩上行，一階又一階，來到城牆頂端。他在人叢中掃視，一點也不在意天空的異象。

    p 最後，他的目光停了下來，停在芬斯特藍的背上。

    p 黑衣人帶著復仇的冷笑，一步一步移向芬斯特藍的位置。悄悄地，他掏出鋒銳的匕首，映著星光，緩緩舉起。

    p 「誰？」芬斯特藍憑著冒險者的靈敏，聽出了身後微弱的腳步聲。

    p 他隨即放脫瓦林娜，將手按上劍柄，同時轉身，準備迎敵。但由於雙腳懸空掛在牆外，一時站不起身。

    p 在芬斯特藍來得及反應之前，對方已比出神秘的手勢，並用冰冷的語調唸出咒文：「麗絲．跨許阿爾」

    p 芬斯特藍忽然感到重心不穩，似乎他所坐的城墩突然被塗了一層油。他已喪失應變的先機了。

    p 不由自主地，芬斯特藍跌下光滑的城牆，往黑暗中墜落，再墜落...

    p 消失於黑暗前，他認出了對方。正是在塵封之殿裡遇到的考古學家。

    p 凶手滿意地笑著，迅即逃下城牆。

    h2 1.3. 踏上旅程
    h3 1.3.1. 醫者安琪

    p 洛南紀五十九年十月十九日，國境東側延海，潛鮪村。

    p 「安琪，」在潮汐之神的廟堂前，白袍的中年修女對年輕修女囑咐道，「異像將在今夜發生，預兆在數週前便出現了。這不尋常...神明在給我們考驗，考驗我們的信仰和力量。大戰即將來臨。

    p 「安琪，今天妳的見習完成，是一名真正的醫士，可以去冒險了。時候到了，安琪，妳不再是不懂事的孩子，而是能夠肩負使命的成人了。

    p 「接到妳那心性不定的表哥即將西行的消息，我一點也不意外。他所信奉的，好戰的浪濤之神，想必給了他什麼特殊的使命。

    p 「妳想跟著他一同西行，到西南山脈最危險、最神秘的地方去，我也不意外。他雖然性格怪異，但對妳一向很照顧。他比妳大，又精通雜七雜八的技藝，妳跟他同行，我也比較放心。

    p 「但是，安琪啊，冒險並非兒戲。外面的世界太多妳不瞭解的事物，像是背叛、謊言、仇恨與殺戮。米庫斯比較瞭解這些，因為他曾出外流浪，經歷過種種挑戰。

    p 「多聽他的，妳會比較安全，但別忘了他信的是混亂之神，而妳要堅持我們的信仰。瑪法達在上，願妳的旅程多災多難，卻都化險為夷，讓妳從中學習，從中成長。」

    p 安琪恭敬地聽著，心思已有一半飄到了傳奇的幻想中。她是一個十八歲的年輕修女，留著一頭黑褐色的長髮，加上古銅色的皮膚，寬而深的臉龐，若不看白袍與圓盾，十足像是東岸的漁民。

    p 她在潮汐之神的教會已見習三年，擅長止血、接骨和草藥，也能靠平衡之神瑪法達的聖恩，來配製靈丹妙藥。

    p 在經歷幾場難治的瘟疫之後，安琪決定踏上探險家的旅程，尋找治癒瘟疫之道，卻一直沒有機會。

    p 最近她的表哥，浪濤修士米庫斯，正式宣佈西行。安琪終於逮到離家冒險的機會。

    p 安琪的父親在她還不會說話時就死於瘟疫。母親改嫁外地，從此失去音訊。安琪對父母的印象十分模糊，但母親留給她的臨別之物，刻花的玉鐲，她卻一直留在身邊。

    p 安琪的童年往在她舅舅家。小時候，最要好的玩伴是表哥米庫斯。

    p 米庫斯十分叛逆，學農事、戰技、教義都漫不經心，反而對讀寫、算術和精工這些沒用的玩意有興趣，讓父母對他失望透頂，卻又難以管教。

    p 米庫斯在家裡不受疼愛，常被打得躲到海邊。因禍得福，他對當地的環境和地形十分熟悉，也對海浪中潛藏的力量有所感受。他從小就愛帶著表妹安琪到處亂跑，談心、說故事。

    p 米庫斯聰明卻心性不定，不管父親的期許，執意進入浪濤神廟，學習如何與司掌暴風與巨浪的混亂之神共舞。於是，在米庫斯十二歲時，父母將他送到風評不佳的混亂神廟處，從此與家庭隔絕。

    p 米庫斯具有浪濤神耐頓堅強、難測、愛冒險等特質，常到村外闖蕩，更是小漁村中許多消息的來源。

    p 安琪還記得小時候坐在礁石上，吹著海風，聽米庫斯講海妖女、水鬼和瓶中信的故事和探險家的傳奇。那時候，她常常聽得心醉神迷，恨不得快點長大，成為故事中的英雄。

    p 現在，安琪的心願，總算有機會實現了。大修女已答應她的請求。

    p 「謝謝妳，大修女，」安琪高興地說，「我不會辜負身上的白袍、項鍊上的雙杖聖徽、以及您賜予我的防身盾牌。我會將潮汐之神的福音傳播到西南山脈的土地上；我會記得您平日的教誨、醫士的誓詞、以及書中的聖言。」

    p 「妳還記得驅散邪靈的禱文嗎，安琪？在未知的險境中，妳用得著它。」大修女慈詳地說道。

    p 「還記得，大修女。」

    p 「那麼覆誦一遍吧，好讓我安心。」

    p 安琪於是舉起胸前銀項鍊上掛著的雙杖聖徽，用嚴厲而神聖的音調唱道：
    p 「邪惡的死靈，汙穢的存在啊，離開這受詛的軀體，回到幽冥的世界！
    p 世間的快樂汝既無福消受，別再徒增憤恨悲傷！
    p 以瑪法達之名，吾命令爾等，
    p 退下！」

    p 一瞬之間，安琪的眼神中露出悲憫的罡氣，似乎可悲的怨靈就在身前。她迅速地恢復了聆聽教誨的虔敬神態。

    p 「很好，安琪，」大修女微笑道，「我原本擔心妳長久從事醫療，已以忘記如何同惡靈作戰了。」

    p 「我還記得，大修女，」安琪道，「不過，我寧願治癒邪惡留下的傷口，而不是以牙還牙。傷害不能改變人心，但是醫療可以。」

    p 「沒錯，安琪，醫者有獨特的使命。」大修女索菲亞道，「不過當敵人欺上門來，妳也不能坐以待斃吧？」

    p 安琪緩緩地點了點頭。

    p 索菲亞緩緩地背轉身去，不讓安琪發現她失控的淚水。

    p 「安琪，去吧，不要擔擱了，」她用儘量平靜的音調說道，「願瑪法達與妳同在。」

    p 年輕的修女轉身離去，眼框中滿是不捨的淚水。

    p 她們兩人都很清楚，亂世即將來臨。今朝一別，或許再也沒有重逢的機會了。

    h3 1.3.2. 詭譎的米庫斯

    p 往村口的路上，安琪觀察著白袍底下的鎖子甲，這是神廟給她的禮物，也是她的第一副甲冑。它並不輕，老實講還滿重的，不過安琪的力氣比許多男人還大，很快便習慣了。

    p 安琪沒有武器，只有背上行囊外背盾、左手繪著雙杖的銳牌，和掛在頸上的，銀質的平衡之神聖徽。

    p 她心想，希望冒險能比血腥的英雄故事更和平。或許，不必和人類戰鬥，只要對付活屍？畢竟，能治癒人心的悲苦、混亂和邪惡，而非殺害他們，才稱得上真正的英雄吧。

    p 米庫斯曾說，冒險者有時會面臨兩害相權的決擇，甚至是在原則與生命之間，或是同袍與生命之間。只有選擇生命的人能活下來，獲得榮耀。傳說中的英雄其實都是滿手血腥、滿腦子背叛的惡棍。不過，這只是他眾多憤世嫉俗的言論之一，安琪並不當真。

    p 想著想著，終於來到了村口。

    p 短髮、矮小的米庫斯正坐在一輛馬車旁，與車夫聊天。看到安琪走來，米庫斯高興地起身。車夫明白即將啟程，便解開栓住馬兒的繩索，坐上駕駛座。

    p 安琪加快腳步走向馬車，突然米庫斯從腰間抽出一把迴力標，向她射來。

    p 安琪料不到他竟會動手，本能地往旁邊閃開，但卻被弧線軌道的迴力標追上。她舉盾去擋，還是慢了一步。只好轉身用背盾擋下。

    p 咚地一聲，迴力標擊中背盾，掉在地上。安琪向前踏了半步，定住身形，驚異地望著表哥。

    p 鎖子甲擋掉了撞擊，安琪毫髮無傷，但還是嚇了一跳。

    p 米庫斯一面撿起迴力標，一面詭譎地笑道：「不錯，不錯，妳有這樣堅實的甲冑，背後還有背盾，簡直像隻海龜，那我也可以放心了。別介意啦，剛才只是開個玩笑而已，還是先上路吧。這趟旅程...會有趣的很。」

    p 「什麼海龜！」

    p 「海龜有什麼不好，海龜很長命啊……冒險者都希望活久一點。」

    p 離開村莊後，安琪就不想計較了，取而代之的是對嶄新景色的注意，及對旅程的好奇。

    p 「米庫斯，」安琪在馬車上問道，「我們這次的行程你是怎麼安排的呢？我很好奇。」

    p 「簡單來說，我們會先延著大路往西到王都諾薩里亞，在那邊待個幾天，打聽消息啦，賺點旅費啦。之後往西經過落雲城，再折而向南到沃土。如果一切順利的話，我們會到西南山脈最新的村落，也就是傳說中的『閃金村』啦。」米庫斯邊看風景邊答道。

    p 「我們總不會一直坐車吧？」

    p 「當然，妳以為我這麼有錢嗎？我們到諾隡里亞後，就得自食其力啦。而且...就算在那兒賺得到錢，乘車經過沃土也不是個聰明的主意。我們可能得雇一小隊傭兵，然後『走』過沃土，到閃金村啦。」

    p 「走過一整個平原？那要多久？」

    p 「可能要花上一個多月啦，安琪。那也算是冒險嘛。沃土可是充斥著強盜和瘟疫的。妳是醫士...瘟疫應該難不倒妳吧。」

    p 「好吧，反正到目的地以前，我就跟著你就對了。現在應該沒有危險吧？」安琪道。

    p 「安琪，」米庫斯笑道，「別忘了，出門在外，『隨時』都是有危險的。我知道甲冑很悶熱，但還是儘量穿著吧。妳現在習慣了，以後步行的時候，才不會撐不住啦。」

    p 「那你呢？」安琪不大服氣，「你的外衣底下也藏有甲冑嗎？」

    p 「這兒不是？」米庫斯拉起衣服的一角，露出內藏的皮製甲冑。

    p 「這東西管用嗎？」安琪不大放心地問，「雖然是加工處理的，可是銳利的刀劍還是能砍破它吧？為什麼不換一副鐵打的？」

    p 「這麼說吧，輕便的裝備對我來說反而比較安全...我不是個喜歡硬碰硬的人啦。對了，妳的武器呢？」

    p 「我只有盾牌，」安琪道，「醫士是不需要武器的。我看你的武器也很怪……」

    p 「但是...」

    p 「危險的時候，禱文就足以克敵了。不是嗎？」安琪道，「我不帶武器，人們就不會隨便與我為敵。」

    p 「可是...」

    p 「你還是多跟我講點關於王都的事情吧，我從來沒去過那兒呢。聽說那裡常有盛大的慶典和競技，是嗎？」

    p 「是啊。不過他們跟我們信仰的神祗都不一樣...那裡啊...」

    p 談笑間，兩人啟程了。但是，這趟旅程並不會一直歡樂。

    h3 1.3.3. 風谷的瓦林娜

    p 洛南紀五十九年十月十九日，王都諾薩里亞

    p 「殺人哪！」瑞福驚慌中大喊。他的朋友芬斯特藍中了怪異的咒術，跌落城下。

    p 人們紛紛靠攏，但兇手早己遁入黑暗。唯一跟得上兇手腳步的，只有看似無害的女孩瓦林娜。

    p 偽裝該結束，冒險該開始了，瓦林娜心道，不息的風會跟上你的。

    p 她輕唸一咒，行囊便縮得像檸檬一樣大小，一手整個揣入衣袋，隨即發足力奔，緊緊跟在灰袍兇手的後面。

    p 對方回頭發現追兵只有一人，於是跑進了一條黝暗的死巷，殺人滅口的好地方。

    p 瓦林娜雙手握住桐木法杖，不加思索地追了進去。對方站在巷子的盡頭，面向著她。
    p 「送死來著？」那人獰笑道，「不過我還捨不得殺妳呢。讓妳睡著好了。」

    p 說完，他唸起咒語：「維兒．夢途．阿」同時掏出一把沙子，撒向瓦林娜。

    p 瓦林娜感到一股矇矓的睡意，但法藝的訓練讓她一瞬間就解開魔咒，恢復清醒。

    p 「睡眠咒！」瓦林娜忿怒地叫道，「這種程度的繫結！你這個低階法師竟敢殺我的同伴！」她怒道，「你以為殺人不用付出代價嗎？你竟然不珍惜自己的生命！」

    p 對方震驚於睡眠咒的失效，以及瓦林娜激動的神情。

    p 他目露兇光，唸出下一個咒語：「麗絲．那西」同時用手指向瓦林娜。

    p 兩顆紫色的光球自指尖射出，逐速地飛向瓦林娜。

    p 瓦林娜悶哼一聲，已受了內傷。她凝視對手，紅寶石的眼中，閃著一絲血色的殺氣。

    p 此時的她，不再是羞怯的女孩，而是呼風的法師。她的神情，沒有絲毫畏懼。

    p 逐雲者啊，她心想，三年來和妳學的一切，現在要展現出來了。

    p 瓦林娜右手拄著法杖，唸出咒文：「普拉絲．提卡．怒殺」。一股旋風便繞著杖尖的翠玉原石成形。

    p 她接著將彎曲左手姆指，握成手刀，在身前交錯兩次，劈出。

    p 排山倒海的疾風如刀，化作看不見的利刃，向前襲去。

    p 十字形的傷口，自敵對法師的雙肩至兩股，以胸腹為中心爆開。鮮血噴灑而出，如一陣紅雨。

    p 那人再也無力施咒，只能顫抖著說道：「妳...妳到底是...誰...」說完倒在地上，抽搐著。

    p 瓦林娜在戰鬥結束時，已恢復冷靜。她完全清楚自己闖了什麼禍：她殺了人，而且是在皇都殺了人。但是，她也知道周圍沒有別人，至少現在還沒有。

    p 「我來自風谷，」她對即將死去的敵人說道，「是修業三年的結界師。我和我的夥伴，都不是你應該攻擊的對象，明白了嗎？」

    p 「風...谷...」那人吐出了此生最後兩個字。

    p 這人也滿不簡單的，能用咒文傷到我，瓦林娜心道，唉，不該讓對手先出招的，我還太嫩了；咒法施得太重，一下就傷了人命，也表示我太嫩了。

    p 「沒錯，風谷，法藝的土地。」瓦林娜說道，「法術之事，還是不要落入旁人手中，免得危險。」於是她跑到那人身邊，開始搜身。

    p 她搜出一些無用的法材、一本魔法書、一把匕首、一袋約二百枚的碎金、一張用細麻繩綑住的卷軸。

    p 她唸出咒文：「麗絲．查維．阿斯」那把匕首隨即發出綠光，代表上面附有魔法。

    p 「安息吧，施法者。」她對不知名的敵人說道。

    p 瓦林娜將卷軸和匕首用牛皮包好後，連錢袋一起放入背袋。草草翻過魔法書後，確定沒什麼特別的咒文，於是連同法材一起點火焚毀。

    p 火苗剛起，她便衝往城門。

    h3 1.3.4. 心靈法藝

    p 門口站著兩名守衛，看守著巨大的木門。

    p 諾薩里亞的城牆比一般城牆高出一倍，城門也十分巨大。

    p 沃土盜賊們聞之喪膽的諾魯夫王曾經讚道，即使兵法高明如他，也無法攻陷這座城池。因此，諾薩里亞得到了「不破城」的稱號。

    p 諾薩里亞四面都有城門，其中南門最大，也最常開放，所以南門才稱「城門」，其他備而不用的門都稱為「側門」。

    p 除了緊急狀況外，側門平常完全不開放。城門則在黃昏時拉上，黎明時再放下，成為護城河上唯一的橋樑。

    p 瓦林娜必須盡速離城，但是城門已拉上。

    p 她衝量了一下局勢，心中已佈好了一個計劃。身為結界師，她決定靠法藝離城。

    p 結界師擅於操縱心靈。睡眠、魅惑、遺忘、暗示都是瓦林娜的拿手好戲。

    p 離開諾薩里亞前，她正是靠這些法術，假扮成風塵少女，向男人們搜集各地的情報，又能夠全身而退。

    p 要這樣搜集情報，是因為心靈法藝在一對一的情境下，才好施展。

    p 同樣的把戲，換個方式表現，她心道，應該不太難。

    p 「哈囉！」瓦林娜走向前，用愉快的聲音說道，「晚安！」

    p 「這麼晚了，別在城門口鬼混，」一名守衛說道，「還不快回家去，小心被綁走了都沒人知道呢。」

    p 「是啊是啊，」另一名守衛接著說道，「還是快回去吧。妳是不是迷路了？」

    p 「我沒有迷路，」瓦林娜故作天真地說道，「但是我回不了家，我家在城外。」

    p 「那我也沒辦法囉，」那名守衛說，「但妳一個人在城裡很危險。這樣吧，我送妳去最近的哨站，請他們讓妳在那兒過夜，明早再出城，好不好？」

    p 「好吧。」瓦林娜不情願地說道，心裡已打好算盤。

    p 守衛帶著瓦林娜，走向側街，又繞了幾轉，離哨站還有一段距離時，瓦林娜抬頭道：「我...我有東西掉在剛才那裡...我可不可以回去拿？」

    p 「妳一個人回去很危險的，」守衛道，「還是讓我陪妳一起去吧。」

    p 「不行啦，」瓦林娜說道，「我要自己去。我媽媽說那東西不能給別人看到啦。」

    p 守衛笑道：「哦，大概是戒指之類的東西吧。妳放心好了，我是捍衛法律的人，不會跟妳搶的。」

    p 「我要自己去。」瓦林娜嘟著嘴，堅持道。

    p 守衛道：「好好好，妳自己去，但是要快點回來喔。」

    p 瓦林娜走過轉角，立刻用全速衝回城門。

    p 城門口只剩下一名守衛。

    p 瓦林娜毫不遲疑地唸出咒文：「維兒．夢露坦」並在空中比劃了一個特殊的手勢，指向守衛。

    p 「妳做什...」守衛的話說到一半，突然停住了。他的眼神變得有些迷惘。

    p 魅惑術生效了。

    p 「妳是誰啊？」他問道。

    p 「我是往在城外的可憐小女孩，如果你不開門我就只好流落街頭，被夜賊綁架了。」瓦林娜平平地說道，沒有偽裝可憐的聲音。

    p 她根本沒有偽裝聲音的必要，中了魅惑術的人很難不信她的話，尤其是求助。

    p 「哦...」守衛疑惑地說道，「所以我應該開門囉？可是有規定...」

    p 「好吧，你遵守你的規定，把我逼得無路可走，讓我落到夜賊手裡，被凌虐至死，你忍心嗎？」

    p 「凌虐至死」四個字對被魅惑的人來說，完全不能承受。守衛不敢吭聲，直接把門放下了。

    p 瓦林娜俐落地走出城門，回頭向守衛說道：「謝啦，不過還是不能讓你記得我就是了。」她唸出咒文：「維兒．提．空．夢提亞」

    p 守衛陷入了失憶的迷惑狀態，此時瓦林娜已走得遠了。

    h3 1.3.5. 降落與會合

    p 洛南紀五十九年十月十九日，王都諾薩里亞城外

    p 三，二，一。

    p 「Ris-Fra-El」芬斯特藍在半空中唸道。他已忍耐了一秒多的墜落。
    p 咒文生效後，下墜的速度緩和下來，卻變得不太穩定。他像一片羽毛般，飄來飄去。

    p 一陣風吹來，芬斯特藍在空中打了好幾個轉，但他已毫不在乎這種暈眩。畢竟性命保住了。

    p 突然，法術時效已過，他又掉了下去。

    p 因為離地已近，在皮甲的保護之下，他落在草地上，毫髮無傷，只是暫時無力起身。

    p 芬斯特藍看向夜空。流星雨雖已減弱，但仍然十分驚人。

    p 他起身，在星光照耀下，小心地環視了四周。

    p 由於風的幫助，他竟已落在護城河外。

    p 不用費力過河了，他想，好運總不會一直離我太遠。

    p 他把沾了泥沙的金髮清理了一下，放低身子，慢慢前行。

    p 確定離城已有一段距離後，他站直身子，加快腳步，走向無盡的黑暗。
    p 在流星雨終於停歇時，芬斯特藍頓覺一陣輕鬆，像是壓在胸口的大石被取起一般。但是，他並未鬆，。畢竟王都附近的夜晚，仍蘊藏著許多危機。

    p 「芬斯特藍！」驚呼聲從左側傳來，嚇了他一跳。

    p 「誰？」芬斯特藍迅速轉身，拔出護身短劍。

    p 黑暗中，一個略為瘦小的身影向他跑來。是瓦林娜。

    p 「你沒死！」瓦林娜手杖一拋，向他衝來，一把將他抱往，「你沒死！」

    p 「對，我還活著。」芬斯特藍笑道，「每個人都有保護自己的責任。況且，妳還需要我啊。」說著將劍入鞘。

    p 「我...我真的很難相信...」瓦林娜將他放開，一面說道，「你怎麼...那種高度...等等，你會羽落術？」她驚訝地問。

    p 芬斯特藍點了點頭：「這叫『羽落術』嗎？我是在古樂譜的字裡行間發現的，不知道它還有個名字。」他隨即好奇地問道，「妳也知道關於魔法的事？」

    p 「我是一名法師。」瓦林娜回身拿起法杖，一面說道，「一名風谷的結界師，修業三年多了。」

    p 一日之間就遇到這麼多奇怪的人，芬斯特藍感到十分訝異。

    p 他問道：「那麼，妳是怎麼出城的？城門沒有開過啊。」

    p 「我是一名法師。」瓦林娜重覆道。這次她帶著神秘的微笑。

    p 「好吧，」芬斯特藍笑道，「不肯說就算了。不過我還是很好奇，在我跌落之後發生了什麼事？。」

    p 「呃，我原本因為你被殺死了...就追上去，和他作戰，後來就把那個人殺了...」她愈說聲音愈小，「可是自己也受了傷。唉，對方雖然不強，卻懂得傷害的咒語。」

    p 「把那人殺了？」芬斯特藍驚訝地問道。

    p 「對，我把他殺了...你不怪我吧？」瓦林娜露出痛苦的表情。這是她冒險以來第一次殺人。

    p 「不，怎麼會呢？」芬斯特藍安慰道，「做都做了，就別再一直去想了吧。況且妳這麼做也不能算錯...他活著會給我們帶來更大的危險。不過，現在我們都和西方考古學家結下了樑子，看來只好同舟共濟囉。」

    p 「嗯。」

    p 「不過啊，妳還真不簡單，竟能將那人打敗。」芬斯特藍道，「對了，妳傷在哪兒？要不要緊？」

    p 「沒有傷口的，」瓦林娜道，「是被魔彈擊中，不大要緊。養個幾天就會好。」

    p 「很不幸，」芬斯特藍道，「此地不宜久留，沒法子好好休息養傷。多待一秒就會增一分危險。」

    p 「可是...」

    p 「這樣吧，我們再走一小段路，」芬斯特藍道，「這附近有許多空的農舍，我們可以在那邊過夜。明早天一亮就動身，去前面鎮上雇輛馬車，妳就可以好好養傷了。」

    p 「為什麼會有空的農舍？」

    p 「唉，」芬斯特藍嘆道，「都得怪那些貴族和皇室的人。幾次天災造成欠收後，租稅反而比以前更高了。那些付不出來的農民，只好逃到西方去。在充滿盜賊和瘟疫的旅程上，真的能重頭開始建立幸福家園的人...一成也不到啊。」

    p 「我們南方的情形就好很多，雖然不富裕，也還過得去，」，瓦林娜道，「想不到王都的農民，反而悽慘。」

    p 「這也是我之所以冒險的原因。在這個時代裡，我身上流的貴族血統不值得驕傲，反而是恥辱和隔閡，阻礙我和探險家一起開創新天地。」

    p 「聽起來很高尚，我會試著理解的。至於行程嘛...」瓦林娜苦笑道，「反正這種事我也拿不了主意，就聽你的囉。」

    p 她從行囊拿出幾片乾麵包，撥了一半分給芬斯特藍，「趁著還吃的到的時候，好好品嚐吧。」

    h3 1.3.6. 芬斯特藍和瓦林娜

    p 時值秋冬交替，夜裡冷到能結冰。今夜似乎特別寒冷，地上已積了一層薄薄的白雪。兩人穿著軟皮靴，踏著積雪前行。

    p 寒冷與黑暗，在兩人周圍凝結成一股無形的壓力。

    p 平原上夜風吹動的沙沙聲、草浪搖曳的黑影，給靜夜保留了些許的背景。如果不是身邊還有同伴，真的就只能伴著星星，伴著月亮。

    p 這時的月亮在半月與圓月之間，而星空則缺了幾個口。災變的恐懼鑽進了國境裡每一雙大大小小的眼睛。什麼樣的命運等在他們前面呢？或許只有司掌宿命的神能回答這個問題。

    p 這樣的夜，自有一番特色，但兩人在殺戮與逃生之後，都失去了欣賞景致的心情。

    p 走著走著，芬斯特藍先開口，打破沉默：「瓦林娜。」

    p 「嗯？」

    p 「我們腳下踩的這片土地，以前都是農田。我小時候常不聽話，出城到田裡去和農家小孩玩。現在兒時的同伴都不知去了何處，也不知吉兇。」

    p 「現在這裡都荒廢了，你一定很難過。」

    p 「唉。」芬斯特藍停頓了一下，又道，「對了，妳說妳來自風谷...那是個怎樣的地方？」

    p 「那裡很美，遠在山脈的南方，」瓦林娜答道，聲音像是飄到了遠方，「有高山、溪流，還有終年的強風。那裡的樹木都很矮，長老說是因為風太大，長太高就會被吹斷，它們才長成這樣。長老還說，有一種圓柏，在谷外的山上也有，但是就能長到參天一般。

    p 「記得有一次，長老把我，還有其他剛滿十歲的女孩，綁在不同的風箏上，放到天空。那是我們村莊的習俗。因為女孩子也是要離家的，和男人一樣。

    p 「一開始我很害怕，一直哭。可是到了天上，看到熟悉的事物都變得那麼小時，我就不怕了。我還記得身在半空中，迎著陣陣強風的感覺。那時，我找到了自己。

    p 「後來我就覺得，人常換視野比較好。這樣就不會陷在單調的經驗裡，永遠做井底之蛙了。我想這也是我們風谷人共通的心情吧。

    p 「婆婆曾說，要像雲一樣飄盪，直到找到降落的地方。不要還沒認識自己，就常住在家，不然家就會變成沼澤，陷住我們的腳步，栓往我們的翅膀，讓我們不能飛翔。」

    p 「嗯，」芬斯特藍專注地聽著，問道，「那妳的魔法是怎麼學來的？」

    p 「是『法術』啦，」瓦林娜更正道，「『魔法』聽起來有邪惡的味道。」

    p 「喔，是，是法術，」芬斯特藍連忙更正，「那妳的法術是怎麼學來的？」

    p 「有一年春天，村裡來了三個人，教我法術的婆婆就是其中一位。他們展現了強大的法力，並教導大家法術，共同研究古代法師的遺產，以將風谷建成一座對抗邪惡的城塞。因為大災難將至，需要團結一致才能對抗。

    p 「婆婆和乘風大師、舞空大師保證不會破壞的村落和傳統，也只在河谷的一個區域活動，留下肥沃的上游及上游給不願加入的人。不過我們風谷人都樂於改變，於是全村，或許全谷的人都加入了吧。

    p 「不過，學法術並不是那麼容易的事，很多人學來學去也學不會，反而覺得很痛苦。於是，他們將自己組織起來，作武藝和射藝的訓練，成了風谷的戰士。

    p 「我比較有天份，所以進步得很快。在通過學徒的測驗後，又修業了三年，成為第二批進階法師。

    p 「後來，由於某些緣故，我就出來了。」

    p 某些緣故，芬斯特藍心道。

    p 「進階法師？那是什麼意思呢？」他問道。

    p 「嗯...這不太好解釋欸，」瓦林娜有點為難地說道，「大概可以用數學來比方吧...學數學的人得先會算術、作圖才能學測量；會了基本的測量才能學三角學、繪製航海圖。」

    p 「所以都是循序漸進？」

    p 「不一定欸。就像占星師需要的數學和建築師不相同，法術也有不同的門類，像我是專研結界與心靈法藝。不過，你說的沒錯，要學習複雜的技藝，得先熟悉簡單的。」

    p 「聽起來妳花了不少工夫。」

    p 「是啊，」瓦林娜笑道，「進階法術很難施展，不管是手勢、觀想法門、咒語聲調都必須一絲不差，否則輕則失效，重則炸成失控的能量，十分危險。好在我們都有自知之明，不熟的法術絕不亂用，只在安全的實驗室裡施展。」

    p 「哇，能施這樣高深的法術，一定很厲害。」芬斯特藍道，「我原本太小看妳了，真抱歉。」

    p 「不過，婆婆曾說，成功的法師不是單靠會施高階法術，更重要的是掌握施展時機，以及如何調配每日記憶的咒文。」瓦林娜道。

    p 「每日記憶咒文...我也是這樣欸，」芬斯特藍道，「自從我在古老的樂譜中找到魔...我是說法術後，我發現記憶它們與熟悉它們真的很難。我必須每天早上最清醒的時候拚命去記，但是在施展出來後又忘了。一開始每天只能記一個，後來...」

    p 「等等，」瓦林娜打斷他，「別告訴別人你能記憶多少，這很危險。法師唯一的武器就是法術，它比戰士的劍還要厲害，但維持繫結的法力有限。所以，如果有人知道法師已經釋放完所有的咒語，那就像看到戰士被繩索縳住了一樣。」

    p 「謝謝妳的忠告，」芬斯特藍道，「但我可不是法師，我對法術還很陌生。」

    p 「哦...所以你劍術高超囉？」

    p 「劍術練過幾年，搏擊也還可以，不過算不上高手。其實我真正擅長的呢，就是...」

    p 芬斯特藍笑道，「賭妳猜不到。」

    p 「你先別說，讓我想想，」瓦林娜道，「不是法術，不是劍技...等等，那個考古學家為什麼找你麻煩？」

    p 「我買了一些他原本想要的古物，又扒了他的透鏡。那是因為最近傳來的消息都指出，西南山脈的考古隊愈來愈神秘，也愈來愈橫蠻，傭兵一僱就七、八十個。我看出事有蹊蹺，才不惜和他們作對，把古物截下。」

    p 「這很好啊...否則他們就會擴張了...聽起來他們不是善類，可能是想獨吞法術奧秘的人吧。師父說，法術會被稱作『魔法』，就是因為有人想獨吞，而不是用來造福人群。」她笑了笑，又道：「不過，你已經洩了底啦。你擅長的是盜賊技巧，對吧？」

    p 「妳很總明，」芬斯特藍笑道，「不過不是。」

    p 「那我就猜不出來啦。難不成你像神廟裡的修士，會靠神禱來展現奇蹟不成？」

    p 「當然不是。」芬斯特藍笑道，「其實，我擅長的是說話。」

    p 「說話？這算一種冒險的技巧嗎？」瓦林娜好奇地問道。

    p 「嗯。這還是很重要的技巧，」芬斯特藍認真地答道，「做為甲冑時，它可以避開不必要的爭鬥；做為武器時，它可以造成傷害和困惑；做為工具時，它可以打聽情報，也可以增加好感。所以說，這難道不是重要的技巧嗎？」

    p 「哦，」瓦林娜不大滿意，「那我也會啊。這不是天生的嗎，難道還有什麼技巧？」

    p 「有喔，」芬斯特藍道，「妳是因為聰明又有魅力，當然常常體會說話的好處--這確實是由於天生的氣質使然。不過，除了氣質之外，我所受的訓練是，瞭解情勢、瞭解對方，再做出決定。」

    p 「什麼意思？」

    p 「比方說，瞭解人心。以前我到西南山脈探險時，深切地體悟到，冒險者最大的敵人，絕不是眼前的危難，而是那些遇到危難就會出賣妳的隊友。」

    p 「你出賣過別人嗎？」瓦林娜擔心地問道，「什麼樣的處境會使人背叛隊友？」

    p 「妳可以放心，我沒有出賣過別人，也不會出賣妳，」芬斯特藍道，「但是，我的確見過，在食物匱乏、敵人強大，或是寶藏價值連城時，人們怎麼背叛出生入死的隊友。」

    p 兩人又陷入一陣沉默，不過這次並沒有持續太久。

    p 「我們休息一下好不好？」瓦林娜問道。

    p 「怎麼了？」

    p 「我...腳底凍得厲害。」

    p 「這樣啊，」芬斯特藍道，「妳怎麼不早點講呢？」

    p 「我想...說不定撐一撐就到了。」

    p 她沒有說出來的是，平常遇到這麼寒冷的道路，她都不是用走的。法師有更方便的途徑。

    p 「那先坐一下吧，」芬斯特藍笑道，「下次別再這樣，有事就早點講。」說完兩人清了清積雪，就地坐了下來。

    p 「來，讓我看看妳的腳，」芬斯特藍溫柔地說道，「希望不嚴重。」
    p 瓦林娜脫下皮靴，露出雪白的雙腳，腳底都凍得發紅了。

    p 「我就知道，」芬斯特藍道，「妳真該早點講的，幸好現在還不太遲。問題就出在靴子上。」芬斯特藍說著，就地採了一把柔軟的雜草，抖掉上面的積雪，順手打鬆後，塞進瓦林娜的靴子裡。

    p 「這樣就沒事啦。」他笑道。

    p 「謝謝你...」瓦林娜不好意思地說道，並把靴子穿上。

    p 「別這樣說，」芬斯特藍笑道，「出門在外，本來就是要相互扶持。妳現在可別謝我，否則以後我們相互之間，不知道還要再謝個幾百次呢。咱們上路吧。」

    h2 1.4. 殺手與神靈

    h3 1.4.1. 冰雹中的三個人

    p 正當兩人準備起身時，瓦林娜突然注意到不尋常的天色。

    p 「芬斯特藍，你看，」她指向前方的天空，「那是什麼？」

    p 一團黑影籠罩了天際。

    p 芬斯特藍凝神細看，注意到左右兩邊各有一大團的烏雲，正向中央靠攏，同時也愈來愈近。

    p 在它們相遇的同時，一道閃電劃開了夜幕，伴隨著震耳欲聾的雷聲，在平原上展現了它的威能。

    p 瓦林娜緊緊抓往芬斯特藍的手，震驚地望著天空的異象。

    p 「雷雨雲！」芬斯特藍驚叫，「不可能！這個季節...見鬼！」

    p 一道接一道的閃電擊向平原。霹靂聲像是巨大的戰鼓，推著整片黑雲吞噬星空，撲天蓋地而來。冷冽的風像是它的前導，吹出了冰凍的氣息。

    p 「瓦林娜，快，」芬斯特藍叫道，「拿出氈子，裹住自己，然後趴下！是冰雹！那可不是玩的！」

    p 瓦林娜沒有見過冰雹，但聽得出勢態緊急，於是立刻照著芬斯特藍的指示，裹好後趴下。

    p 在第一批疏疏落落的冰雹落下的同時，芬斯特藍和瓦林娜兩人，只能儘量保護自己。什麼法術、劍術、話術，此刻都派不上用場了。

    p 接著冰雹愈下愈大，像從天而降的石雨。瓦林娜和芬斯特藍雖有毛氈護身，但還是不時被較大的冰雹砸到。兩人咬緊牙關，苦苦忍耐。

    p 這時，他們注意到，不遠處傳來一個人聲，愈來愈近。

    p 芬斯特藍凝神細聽，聽到一名男子咒罵的聲音：「死老天！你再下冰雹啊！下大一點啊！死老天！你以為我怕你不成！」

    p 那聲音愈來愈近：「死老天！我烏風空有一身功夫，竟然落到這種下場！月影的殺手我都幹掉了！你再來啊！死老天！我會連你一起宰了！」

    p 吼聲中摻雜著被冰雹擊中的呼痛聲，愈來愈近。

    p 被月影追殺的人，芬斯特藍心道。他怎麼逃得到這裡？無論如何，不能讓他就這樣死去。

    p 他忍著冰雹擊身的痛苦，拿下身上的毛氈，捲成一團。

    p 「朋友，接著！」芬斯特藍叫道，隨即將毛氈丟了過去。

    p 瓦林娜不得已，只好張開身上的毛氈，將兩人一起蓋往。她雖然對芬斯特藍很有好感，但一點都不想如此靠近。

    p 她不太高興芬斯特藍不問她就直接丟出毛氈。一瞬間她不禁想唸個咒語來給他一點教訓。不過，仔細一想，確實這是救人的唯一辦法，於是收回了剛要放出的法術。

    p 芬斯特藍感覺到少女的身體貼著自己，忍不住心跳加速。不過，考慮到她是掌握力量的法師，也是冒險的同伴，他隨即克制自己，專心觀察冰雹中那人的反應。

    p 冰雹太大，他什麼都沒看到，不過吼聲停止了。看來對方似乎接到了他的救命禮物。

    p 「芬斯特藍，」瓦林娜輕聲道，「那人接到毛氈，躲到草叢中了。」

    p 「妳看得見？」芬斯特藍訝異地問道。

    p 「嗯，從小就有異於常人的夜視力，也不知道為什麼。噢，好冷。」

    p 由於過於寒冷，他們不再說話，只是等待。

    p 冰雹下著下著，漸漸地緩和了。雷雨雲也慢慢遠去，只留下遙遠的隆隆聲，還不時傳來。

    p 終於，一切歸於寂靜。

    h3 1.4.2. 烏風

    p 芬斯特藍和瓦林娜拖著僵硬的身驅起身，全身上下都被砸得疼痛不堪。

    p 那人也迅速爬了起來，朝他們走近。

    p 他被著一件黑色的斗蓬，沾滿白色的霜雪。臉上全用黑布蒙住，雙眼罩著黑紗，從布的缝繫中露出幾撮凝著水滴的銀髮。整個人看上去就像一具剛從冰棺裡挖出來的屍體。

    p 「我叫烏風，感謝你們救了我。」他冷冷地說道，聲音沙啞而低沈。

    p 瓦林娜感到一股強大而深遂的懼意，來自內心的極深處。

    p 他只是個人，她告訴自己，只是個亡命之徒。

    p 「不敢當，只是巧合而己，」芬斯特藍道，「敢問閣下是何人？為何會被月影公會追殺？為何出城甚遠，卻不帶行囊？」

    p 「無可奉告。」烏風用他一貫冰冷的聲音說道，說著將毛氈遞還了芬斯特藍。

    p 「別這樣，朋友，」芬斯特藍道，「你我同是天涯亡命人。說實在，我們也正被人追殺。」

    p 「往哪裡去？」烏風問道。

    p 「西南山脈。」

    p 「探險？」

    p 「對，而且還背負著任務。要同行嗎，朋友？」

    p 「你們會有危險，」烏風冷冷地說道，「大批月影的人正追捕我。」

    p 「怕什麼，」芬斯特藍道，「反正我們原本就夠危險的了。」

    p 「你們讓我同行？」

    p 芬斯特藍點點頭，轉向瓦林娜。

    p 「當然...不過，我們可以誠相待嗎？」瓦林娜道。

    p 「哼，」烏風冷笑道，「我答應不向你們說謊便是。」

    p 芬斯特藍很清楚，對於烏風這種人來說，這便是友誼的保證了。

    p 他們一邊往前，一邊相互詢問往事，以及被追殺的原因。

    p 芬斯特藍和瓦林娜簡單地描述了之前的種種經歷，以及關於考古學家的怪事。

    p 「換你了，」瓦林娜笑道，「要以誠相待。」

    h3 1.4.3. 烏風的過去

    p 「我曾是一名殺手，」烏風道，「在月影底下工作。」

