      p 童年的歐奇力婭，生活很貧窮，從小就要幫媽媽顧攤賣點心、玩具。她的法藝在七歲時顯露。
      p 當她發現自己能用手碰水就將它變成冰時，歐奇力婭十分高興。她也因此多了一項比較賺錢的本領：在夏季表演空手製冰。
      p 十歲時，一場意外改變了歐奇力婭的命運。
      p 那是一次監獄中有三名殺人犯越獄逃亡，其中一名還是流浪術士，能施法。當歐奇力婭聽到消息時，還在販賣她的冰品。她並不害怕。
      p 當三名逃犯真的經過這條街時，歐奇利娜集中能量，施出寒冰錐，將三人一起冰封了。
      p 愛森大法師之塔的人聽到消息，立刻派人來巡訪這名自學者。十歲的女孩，無師自通竟能施展高難度的寒冰錐，很快歐奇力婭就成為了法師學徒，學習讀寫、組合各式各樣的法材咒詞、還有算數與歷史。
      p 在法藝大多由男性傳承的時代，歐奇利娜成為了首位女性的塑能大師。每個世紀總有一兩位女性擔任守護大師、結界大師或預言大師，但是司掌純粹力量的塑能大師不曾。這也讓她備受矚目。
