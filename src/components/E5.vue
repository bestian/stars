      p 雖然接觸廣泛，歐奇力婭還是偏愛寒冰咒術。冰可以熄滅混亂之火，冰可以冷卻無名妄動，冰也可以溫暖人心。
      p 施展轉生術後，在地底孤寂的長夜裡，歐奇力婭的心，漸漸像冰一般樣凝結，堅實地等待。
