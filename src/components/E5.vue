      p 有幾年，歐奇力婭學會了靜心，讓漫長的等待成為一種觀照內在的過程。千年的內觀。
      p 她的心變得很平靜。生前的衝動、傲氣和執念，在歲月的煎熬下，釀成了徹底的平靜、透明的智慧。
