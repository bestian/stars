      p 她一舉步，就發現自己竟然穿著一件笨重的甲胃。鎖子甲。
      p 為什麼沒有先把屍體的甲胃脫掉再送進來呢？
      p 她慢慢脫掉鎖子甲，又發現自己頸上竟佩戴著一串銀項鍊，上面鑲著銀質徽記，是兩根交叉的杖子。
      p 歐奇力婭不認得這個徽記，於是把它解下，收進行囊。
      p 行囊裡面有藥水、符咒、藥療用具、小錢袋、骷髏面具、小鏡子、還有一些布、小鍋、小鏟、叉子、小刀、火種、火石、乾糧、飲水、鹽巴以及許多不知名的藥草與香料。
      p 只有牆壁上的螢光苔在微微泛著綠光，歐奇力婭希望亮一些。
      p 火石…不對，她心道，我是法師啊。
      p 「光噴古。」空中冒出了火把一樣的光亮。
      p 歐奇力婭拿起鏡子，打量了一下自己的面孔。
      p 太年輕了，可能還不到二十歲，古銅色的皮膚，眼睛和面部輪廓像是東部人。只有髮色是對的，黑褐色，可是太長了，也沒有綁成馬尾。
      p 「錯形物換身始突風越。」
      p 鏡中的人影，開始改變。膚色變白、頭髮變短、身高變得稍矮一些、肌肉也不那麼強壯。歲月的痕跡也出現了，現在的歐奇力婭回到四十多歲的模樣，生前的樣子，絲毫不差。
      p 她又唸了幾個咒詞，召喚出整套的內外衣物，換上。這樣就和生前一樣了。只差大法師之袍與冰晶法杖。
      p 她記得這兩件法器放在墓室的最右側的暗格裡。果然，沒幾下就找到了。由於法力的加持，大法師之袍和冰晶法杖竟然千年不壞，只是沾了灰塵。歐奇力婭用布將它擦拭乾淨。
      p 久違了，老友，她心道，現在我們可以再次冒險了。為了不屬於我們的一個時代。
      p 披上長袍，將扣環扣上。執起法杖，懷念的重量。
      p 歐奇力婭思量片刻，決定不必自己喚醒其他的大法師。尋找神球比較重要。
      p 她不喜多言，不想敘話，於是輕輕唸了一個咒，遁出了古墓，到達原野和藍天。
      p 一個久違的世界。
      h3 5.5.4. 傷逝
      p 洛南紀六十年三月十八日，愛森古墓
      p 「她一直沒出來。」彼德痛苦地說道，「已經過很久了。」
      p 「很難想像我爸會有什麼反應，還有集漁鎮的修女索菲亞…」米庫斯嗚咽著，「他們把她托付給我，我卻把她帶到死亡。天哪。」
      p 瓦林娜不知如何是好。這場悲劇可說是烏風的獨行造成的，而烏風是她的同伴。
      p 她感到悲傷、不知所措、還有自責。
      p 不知道烏風回來會怎樣？風訊已讓他知道安琪的犧牲，他可能會調整好情緒才進來吧？
      p 或許他進來的時候，就像平常一樣冰冷。但那樣更糟。如果激怒了彼德跟米庫斯，他們或許會攻擊他。
      p 是了，瓦林娜心道，我可以用咒詞來撫平他們心中的痛苦和悲傷。
      p 但是她很快打消了唸頭。這是不敬，對安琪的不敬。
      p 三個人的思緒都很紛亂，心情跌到谷底。彼德尤其悲傷，因為他錯過了一個救她的機會。
      p 如果還能再見到安琪，彼德心道，我一定要告訴她我有多愛她。
      p 如果沒有大戰、如果沒有使命、如果沒有大法師，就我們兩個人，在隱者庭苑過幸福的日子，不是很好嗎？為什麼人要冒險呢？為什麼結局會變成這樣呢？
      p 為什麼她可以完成她的堅持，然後讓我活著受苦呢？
      p 不公平，彼德心想，太不公平。如果角色對調，可能會比較好。但是太遲了，一切都太遲了。
      p 「故事還是要繼續下去，」彼德喃喃自語，「不管有意義還是沒有意義，我們都要喚醒大法師。去他的古墓！來，喝酒！」
      p 他從行囊中拿出家鄉的葡萄酒，放到地上。接著坐倒在地，看著綠色的慘光映著酒瓶。
      p 「來，喝酒！」彼德拔下瓶塞，灌了一大口，「米庫斯，來！喝到爛醉，醒來就會發現一切都是夢！」說著他又哭了起來。
      p 彼德又灌了一大口，卻一口氣岔到，嗆得把酒噴在地上，猛咳不止。
      p 「彼德，別丟人現眼的！」米庫斯一面敲著石壁，一面說道，「你就不能安靜一點嗎？」
      p 彼德一面喘氣，一面說道：「安靜是可以啦！那我一個人去散一散，不打擾你。失陪了。」說著他走出房間，進入通道。
      p 房間裡，沒有人說話，只有米庫斯急促的呼吸聲。
      p 時間一分一秒地過去。通道那邊不時傳出幾聲狂吼。
      p 突然，有沉重的腳步聲接近，瓦林娜和米庫斯往通道方向望去。沒有人影。
      p 腳步聲進入了房間。突然地上憑空出現一具屍體。
      p 「烏風！」瓦林娜道。
      p 但是烏風沒有回答。在隱匿之球的保謢下，他悄悄地離開了。
      p 瓦林娜著手調製藥劑。她從行囊掏出一個玻璃瓶，倒入八分滿的古墓黑藥，然後用小刀劃破左手無名指，滴了幾滴鮮血。
      p 她舉著瓶子搖晃，直到血液與完藥劑完全混合。
      p 然後是注入屍體的口中。
      p 瓦林娜感到十分困擾：為什麼是我？為什麼是我？每個人都很沮喪、很悲傷，不是嗎？為什麼現在是我一個人去調藥灌屍體？
      p 當藥劑注入的同時，瓦林娜注視著屍體的臉孔。大約二十歲、皮膚曬得很黑、方臉、黑色捲髮、鬍子很久沒刮、像團亂草。
      p 「每一個人都有所愛的人，每一個死亡都是傷心之事。沒有比較、沒有權衡、也沒有誰能犧牲別人的生命。」安琪曾經這麼說。
      p 沒有比較、沒有權衡。
      p 但是當天降星使、大難來臨，有能力與之抗衡的人，就比較重要。至少對旁人來說是這樣。
      p 這是為什麼要喚醒大法師，即使犧牲無辜也再所不惜。即使行惡，但是換得更大的善。這就是權衡。
      p 但是對被犧牲的人來講，那就是一切；對愛他的人來講，那就是一切。
      p 瓦林娜漸漸明白安琪的意思。
      p 藥，一點一點地注入。直到瓶子倒乾。
      p 瓦林娜拖著屍體，走向青銅門。米庫斯一言不發，起身抬著屍體的另一端，一起走向轉生之殿。
      p 門裡，還是一片黑暗。

