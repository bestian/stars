      p 那酒鬼約三十來歲，滿臉鬍渣，看起來一點英氣都沒有，和占卜師鐘墨芝石形成強烈的對比。
      p 「這是一件可悲的事，也是一線希望，」鐘墨芝石緩緩地說道，「在千餘年前，愛森文明一度毀於神怒。這你們都知道了吧？」
      p 「知道。」瓦林娜道。
      p 烏風仍保持沈默，並用隱匿之球藏住自己的形跡。
      p 「你們應該也知道，愛森時代的八名大法師，在文明毀滅前，共同鑄造了八枚神球，將畢生的法力注於其中，讓未來的英雄，得以用它們來扺擋星使吧？」
      p 「是的，」瓦林娜道，「隱匿之球已經被我的同伴控制，而寒冰之球落入野心勃勃的法師寇維爾手中，他是秘密組織寒冰會的首領。其他的神球呢？」
      p 天色漸暗，旅店的客人各自回房，只留下瓦林娜等人。在壁爐明滅不定的火光中，鐘墨芝石繼續述說著秘密。
