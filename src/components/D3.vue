      h3 4.3.2. 被怪物趕上
      p 洛南紀六十年二月八日，山道
      p 連著數日的逃亡，讓閃金村的人民疲憊不堪。雖然腳步已經加快，還是被怪物趕上。
      p 米庫斯指導大家設下的陷阱，使村人免於全滅。落石掩埋了怪物的先鋒部隊、路障拖延了牠們的腳步，火焰與洪水的陷阱，使怪物們在接戰前，就死傷慘重。
      p 但是，接二連三的山崩、落石、絆腳索、路障、火焰與洪水陷阱，仍然無法粉碎潮水般的攻勢。怪物的數目太多了。
      p 大飛熊、螳螂妖、噴火蜥蜴還有各式各樣叫不出名子的怪物如飛蝗般湧來，帶著痛惡星使賦與牠們的憤恨與殘忍，帶著無窮盡的妒念，和殺戮的慾望。
      p 惡夢般的景象，讓所有在場的人戰慄。在森林的山道，人類成了妖怪的獵物。殺戮戰場上，連五歲的小孩都拿起刀，抗拒潮水般的怪物。
      p 整片森林充斥著殺伐聲、哀號聲、吼叫聲，每一棵樹幹上都濺滿了血。
      p 背水一戰，讓每個人發揮了超凡的力量，終於將怪物逐退。
      p 但是，戰鬥結束時，老弱婦儒已傷亡大半。斷後的警備隊死傷慘重，隨行的探險家也傷亡殆盡。
      p 警備隊長力戰而亡。
      p 雖然米庫斯認為這是他不聽勸告，拒絕帶大家逃往隱者庭苑的報應，但是隊長在戰鬥中身先士卒，拼死保護村人的勇氣，也讓他不得不佩服。
      p 沒有時間安葬逝著，傷者們必須趕快前往安全的盛恩村。
      p 由於探險家營地和閃金村都沒有醫者，安琪的醫術發揮了關鍵的作用。她對受傷的人，不分貧富、不分老少，無不貼心地照顧。
      p 安琪的醫術，使帶傷的人免於一死，溫暖了受創的心靈，也指導村人們治傷的技巧。因此，她獲得人們高度的尊敬。
      p 「我寧願把心力花在治傷，將戰場留給擅戰的人，」安琪道，「再多的復仇也換不回失去的生命。」
      h3 4.3.3. 瓦林娜、烏風、雷、嵐和卡穆里。
      p 洛南紀六十年二月九日，諾薩里亞
      p 在海之擁旅店，瓦林娜和烏風將小孩們託付給了店主卡穆里。
      p 「雷、嵐，你們就在這裡住下吧。」瓦林娜對孩子們說道。
      p 這一路上，名叫雷的男孩，已經和烏風學會了基本的潛行技巧。當然，他還是無法在夜裡偷襲到烏風。
      p 他的妹妹嵐，在旅途中常被惡夢驚醒。被餓鬼包圍的經驗太恐怖了。
      p 瓦林娜送給了嵐一本法術書，是在寒冰會時拿到的。嵐才八歲，但是已會讀寫，對法術的領悟力也非常高。在短短幾天的旅程中，她已經學會『閱讀魔法』、『偵測魔法』和『小戲法』了。
      p 「我還想跟著你們，妳還沒教我隱身術呢。」嵐不捨地說道。
      p 「不行，那太危險了，」瓦林娜道，「如果妳真的想學，就去看我留給你的書吧。那裡面有很多法術，夠妳學個兩三年的。裡面也有記載隱身術的咒文，一頁一頁認真唸，有一天妳能自己學會的。」
      p 「真的嗎？」小女孩懷疑地問道。
      p 「當然是真的，」瓦林娜笑道，「好的書本加上妳自己的悟性，就是最好的老師啊。」
      p 「好吧。」嵐不情願地點點頭，想了一想，輕輕唸了一個咒文：「Vil-Val-Vu」
      p 兩團紅色的煙霧從她指尖噴出，化為兩個愛心的圖案，圈住烏風和瓦林娜。
      p 「喂，妳這是…」烏風道。
      p 嵐微笑著眨了眨眼，轉身和他哥哥一起，跟著卡穆里走進房間了。
      p 其實，由於精靈和闇精的血液相扺觸，烏風和瓦林娜雖然一起冒險，卻沒有十分親近。一旦靠近，身心便會相互排拒。擁有精靈和闇精血液的人能相互照應，成為夥伴，已經是奇蹟了。
      p 至於雷和嵐，瓦林娜和烏風都不知道，在遙遠的未來，他們會成為名聞天下的冒險者，在灰燼的年代扮演舉足輕重的角色，但那是另一個故事了。
      h3 4.3.4. 聖王的血脈
      p 處理完小孩的事，瓦林娜和烏風在旅店中坐下，將連日的疲累暫時忘卻。
