      p 「精靈？為什麼要防止精靈？」瓦林娜疑惑地問道。她自己身上就有一半的精靈血統，和精靈族特有的，紅寶石般的眼睛。
      p 「古代的精靈是單純的，但後來不再如此。」白袍的占卜師道，「精靈們曾經是善的，但後來一度墮落。在上一次末日之戰中，精靈加入了天神的一邊，成為愛森的敵人。」
      p 「為什麼？」
      p 「人類太不穩定，也太不可靠。」鐘墨芝石道，「過於短暫的生命，讓人類變化快速、難以捉摸。在愛森文明盛極一時的那個年代，所有其他的種族，特別是精靈和矮人，感受到強大的威脅。在人類掌握了高階法術的奧義後，這種威脅愈來愈恐怖。」
      p 「因此，當諸神的使者找到精靈王，也就是撰史者阿法利烏斯時，祂們沒有花多少力氣，就說服了精靈王，出兵加入戰局。」
      p 「精靈王持著陽焰劍和月影刀，帶領森林的子民，讓愛森人聞風喪膽。」鐘墨芝石愈講愈悲哀，「愛森的大法師們雖然高強，但是阿法利烏斯是世上最古老的古精靈，一言一語、每字每句都是法術。人類法師苦苦鑽研讀的一切，都只是在摹彷古精靈順乎自然的語言和姿式罷了。」
      p 「呃…你的意思是，我在風谷還有沃土努力學會的一切，對古精靈而言完全是自然而然的？」瓦林娜不禁有些沮喪。
      p 「正是如此。古精靈用法術來建立精靈的國度，就像風、水、雨、露那般自然。他們擁有如此的創生之能，和同樣強大的毀滅之力。這樣妳就能理解，為什麼愛森時代的法術雖然高明，足以讓星使敬畏，但還是敵不過古精靈和精靈王吧？」
      p 「嗯。」
      p 「大法師們自知難逃毀滅的命運，鑄造了神球、留下了遺產，最後將自己埋葬在不知名的某處。」占卜師道，「但是，除了神球之外，他們還鑄造了一頂八神球，稱作愛森聖王之冠。」
      p 「聖王之冠？」瓦林娜問道。對一個在風谷長大的人而言，『聖王』這個詞有點陌生。
      p 「是的，聖王之冠。」鐘墨芝石道，「只要流著愛森皇族的血，戴上這頂八神球，就能成為領導世人、對抗星使的聖王。」
      p 「這怎麼可能？」
      p 「瓦林娜，身為結界師，妳應該知道法術可以加持物品吧？」鐘墨芝石道，「高強的加持術，是可以把智慧和記憶，一起鎖進八神球中的。」
      p 「但是，都已經過了千百年，誰還知道誰流著愛森皇族的血呢？」
      p 「宿命星使知道。」占卜師道，「而我，身為九神球之一，『真知之球』的主人，我也知道這個秘密。不過紫焰比我早一步，派手下的修士暗殺了全部的皇族後裔。你們眼前的這個酒鬼，是唯一的倖存者。」
      p 「就為了一頂聖王之冠，所以紫焰要殺他，而你又救了他？」瓦林娜訝異地問道，「現在，我猜你是要我們保護他，或著是去找八神球吧？」
      p 「沒有錯，」鐘墨芝石道，「為了人類。」
      p 瓦林娜陷入了沈思。血統、後裔、聖王、酒鬼，許許多多的念頭在腦海裡打轉。
      p 壁爐的火光閃爍著。
      p 「我不答應。」烏風從陰影中出現，冷冷地說道，「太荒謬了。」
      h3 4.3.5. 『果皮香蕉』
      p 洛南紀六十年二月九日，盛恩村
      p 閃金村的人民來此已有兩天，也得到妥善安置。
      p 充份休息之後，冒險者聚集在「果皮香蕉」旅店的大廳，交換來自各地消息。
      p 「果皮香蕉」的名稱雖然滑稽，但今天的氣氛並不好。來自各地的人，有著各自的不幸、各自的苦難。所有的人臉上都是憂愁、都是沮喪。
      p 大廳裡最醒目的是一名英氣十足的女子，約二十六七歲。她頭髮剪齊，腰間佩著一把彎刀。而身旁牆邊的鐵盔、重鎧和長鎗，看得出是一名騎士。
      p 她身旁是一名身材略瘦的騎士，和一個帶著短琴的人，看樣子像是說故事的詩人。
