      p 「我來了。」他說道。隨即觀察他的神兵落在何處。很快地，他注意到祭壇上擺著的戰鎚，那正是他的神兵──定罪者。
      p 太詭異了，玉輝心道，怎麼會那麼順利？像是安排好的一般。
      p 他拿起神鎚，轉身面對那兩名修士。
