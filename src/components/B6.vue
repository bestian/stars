      p 「這對冥王有什麼好處？這恩惠不應白給。」玉輝問道。
      p 「冥尊要請大人重新解開幽冥之門的封印。」
      p 「幽冥之門」是連結幽界與凡界的通道，和連結天界與凡界的「天橋」被稱為「兩大禁地」，上面有著重重封印。
      p 千餘年前，遠在末日之戰之前，當愛森文明仍在盛世時，幽冥之門的封印曾一度被解除，導至幽界生物大舉入侵，後來北斗神尊將聖鎚「往生」賜予凡人，才再度封印了幽冥之門。
      p 「開啟禁忌的封印？」玉輝道，「那中土豈不是要被幽界併吞？」
      p 「卻又如何？大人的任務應該只是要毀滅人類，並非要保全中土吧？」
      p 玉輝沉默不語。
      p 過了一小段時間，他作好決定，向彌爾道，「帶我去見冥王。」
      h3 2.6.4.吞併
      p 洛南紀五十九年十一月二十五日，鬼鎮
      p 小鎮已幾近荒廢。僅在一個多月前，這裡還是個相當繁榮的地方，卻在一場災難中成為廢墟。
      p 在隕星之夜，那最為恐怖的夜晚，一隻屍妖從冒火的旅店外衝到街上，見人就殺，當者披靡。村民們死的死，逃的逃，轉眼這座小鎮原本的名字就被人遺忘，代之以「鬼鎮」。
      p 現在唯一還住在鬼鎮裡的，是一個披著紅色法袍，留著黑色山羊鬍，戴著項鍊的人。他的項鍊上掛著一顆黑色蛋形帶暗紅色斑痕的寶石，那是他從可憐的沃托人手中取回的寶物。
      p 珠寶商卡藍傑用低價向米庫斯買到了暗血石，卻在三天之後被原主奪走了。更慘的是，他的生命也被奪走，成了行屍走肉。
      p 除了卡藍傑之外，鬼鎮還住著許多其他類似的東西。牠們不是人，而是活屍，像是屍妖、僵屍還有餓鬼，都是法師以暗血石的力量製成的。
      p 現在，這名披著紅袍的法師正拿著一張字條，用唯一能見的右眼讀著。那是一隻蝙蝠送來的信，上面用紅色的墨水寫道：
      p 致凡軀的闇火法師庫那西：
      p 載你們來中土的薩瑪蘭公爵決定將三顆石頭給我 今晚我便來拿你的 並且你將告訴我另外兩人的去處 否則我會連你的性命和石頭一併取走 別想亂來 別想走
      p 玉輝
      p 這個狂妄自大的傢伙……獨眼庫那西心裡怒火中燒。但是，他知道鬼船船主的名字……看來是有點來頭。
      p 無論如何，不戰而降太屈辱了。況且，我可是名身經百戰的闇火法師呢！能通過考驗，取得暗血石使用權的，都不是普通人，尤其我是我們裡面最強大的施法者。當然那個月影公會的影法師羅德諾也會施法，但他的法術實在不怎麼樣，比起他的身手來講，差多了。
      p 無論如何，待會兒就要與這個玉輝一戰了。我會讓他知道厲害的，他心道。
      p 庫那西走到焚屍廣場的中央，在這充滿焦臭味的土地上用暗血石畫了一個大型的保護圈，也就是圓形內接一個六角星。
      p 他一邊畫一邊唸著神秘的咒文，而暗血石則隨著念咒聲不斷冒出綠色的火焰，在地面上熔出綠色的痕跡。
      p 準備就緒，他發出一聲鬼哭一般的叫喚，將活屍們從鬼鎮的各處集結起來，等待著即將來臨的一戰。
      p 當太陽的光芒漸漸被東方升起的黑夜逐入地底，在夕陽的方向，出現了一個散發著白光的身影，乘著昏暮的晚風，飛向鬼鎮。那正是玉輝。
      p 玉輝逐漸飛近，準備在最短的時時間取得暗血石，交給吸血公爵薩瑪蘭這個冥王器重的手下，以換得冥王的協助。
      p 不過，當他愈飛愈近，便發現了集結的活屍和保護圈中的暗火法師。看來對方並不怎麼打算乖乖地交出寶石。
      p 「凡人！」他喊道，「我乃審判之神的星使玉輝，不是你可以面對的敵人！快交出暗血石，不要抵抗！」
      p 回應他的是一團紅色的火球。
      p 玉輝雖披著超凡的星辰之袍，還是受了一些傷害。熾熱的火焰讓他感到不舒服。
      p 事實上，這顆火球的威力足以將一個小隊的士兵烤成焦炭。
      p 「所以你決定一戰？愚昧無知的凡人！」玉輝說著將定罪者一揮，一道閃電向闇火法師射去。
      p 只有最強壯的戰士，才挨得住這道強大的閃電束。但是，當它射上保護圈時，卻在空中爆開了。火花在圈上亂竄，卻沒有傷到法師分毫。
      p 庫那西唸出咒文：「Pras-Kon-Nasee」並揮動他的法杖。
      p 杖尖又射出一顆火球，不過玉輝有了防備，將星辰之袍擋在身前，阻住烈焰，沒有被燒傷。
      p 「好個袍子！」法師咒罵道。
      p 「好個圈圈！」玉輝吼道。
      p 獨眼庫那西唸出咒文：「Vil-Nasee-Os」。他的杖尖射出三枚綠色的火焰彈，那是焚靈火製成的。
      p 玉輝用袍子擋掉了一枚，卻被另外兩枚擊中。由於再度受傷，他感到非常憤怒。
      p 「受死吧！」他大吼道。他從空中揮出一鎚，引下一道天火，燒上了庫那西。
      p 但是，這團致命的烈火不但沒有讓他倒下，反而像是變把戲一般，燒上了保護圈，毫無效果。玉輝聽到法師在烈焰中大笑的聲音。
      p 這時，活屍們也注意到了這場戰鬥，不過他們不會飛，只能對空狂叫，發出駭人的吼聲。
      p 法師又唸出了焚靈彈的咒文，這次有一枚擊中。
      p 玉輝吃了幾次虧，決定不再用法術攻擊，直接伏衝而下，準備一鎚擊倒對方。
      p 「來得好！」庫那西叫道。
      p 闇火法師很清楚自己的保護圈只能對抗法術，無法阻止對方飛進來。他只能靠自己，還有暗血石的力量。
      p 他舉起寶石，描準飛近的星使，集中精神，將寶石恐怖的力量釋出。
      p 源源不絕的綠色火焰成錐狀噴出，將星使整個罩住。他儘可能地用袍子擋住烈火，並加速衝住法師。
      p 綠色的烈焰讓玉輝什麼也看不到，只能繼續向前。他感到焚靈火不斷灼燒著他的肌膚，以及他的靈魂。他憑著對不朽之軀的信心，決定一賭。
      p 法師也別無選擇，只能繼續釋放焚靈火，希望能對方在飛近前就將祂擊倒。那時，星使或許會變成一個強大的活屍，聽命於庫那西。
      p 不幸的是，星使異常堅軔。他雖已受了傷，又處於致命的烈焰中，但還是衝過火焰，擊出了關鍵的一鎚。
      p 關鍵的一鎚。
      p 只一鎚，就讓庫那西徹底崩潰了。他喪失了視覺、聽覺，並失去了再戰的勇氣。這一切都是因為定罪者的超凡法力。
      p 現在，可憐的法師只能絕望地站著，什麼也不能做。
      p 重傷的玉輝平安落地，不過馬上被一群兇惡的活屍包圍。
      p 雖然身受重創，但玉輝絲毫不怕眼前的活屍。若是吸血公爵薩瑪蘭親自來到，以他的傷勢，恐難抵敵，但眼前這些只是低等的活屍而已。
      p 他將定罪者用力一揮，一股來自邪神的力量便襲向活屍，逼使牠們服從玉輝的命令，而不再效忠原主。
      p 對於以能量支撐的活屍，沒有忠誠與信義可言。一當支撐牠們的能量受到影響，牠們便會立刻改變。
      p 來自善神的正能量，能使牠們畏懼，甚至直接解除牠們受的詛咒，讓牠們的靈魂往生，肉體死亡；若是邪神的負能量，則能令牠們效忠。
      p 玉輝喘了口氣，將神鎚輕輕一揮，靜靜吐納。
      p 一股治療的力量注滿了他的身軀，傷勢竟在幾秒之內就痊癒了。
      p 玉輝將鎚子輕觸到法師身上，回復了他的視覺和聽覺。
      p 「好了，」玉輝拿下法師的暗血石，說道，「告訴我另外兩人在何處。」
      p 法師無力拒絕，也無法拒絕。
      h3 2.6.5.掙扎
      p 洛南紀五十九年十一月二十六日，沃土
      p 烏風即將饑渴而死。若不是以前在月影時常服用微量的毒藥，褐刺劍上的毒早就能要他的命了。
      p 當他從瘋狂的狀態回復時，他的心智己受到了嚴重的創傷。當他連續好幾天沒有食物時，他原始的本能開始提升。
      p 他又盲又聾，靠著觸覺和嗅覺，還是找到了一棵可以遮蔽的樹，雖然它的葉子和樹皮都極為難吃。
