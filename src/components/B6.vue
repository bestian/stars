      p 他與另一人同時鞠躬。
      p 「你們不是我主神的修士？」玉輝十分訝異，「那怎能喚我下凡？你們有何意圖？」
      p 「『意圖』？」那人微笑道，「冥尊只是希望能與你見個面，談一談。啊，我忘了自我介紹。我是冥尊座下死冥術士彌爾，這位是召喚師穆。」
      p 「你們不是生者吧？」玉輝開始感到有趣了。至少，在這廟中，他的神力仍佔有絕對的支配地位。他無需害怕。
      p 「不錯，」彌爾道，「我們是吸血鬼。當我們脫離凡軀，成為新的生命形式時，原有的法力並未喪失。」
      p 「吸血鬼！」玉輝吃了一驚，「你們不應該出現於凡界的。」
      p 「是的，大人，很抱歉，」彌爾恭敬地說道，「但是，我們只是奉命請大人下幽界一趟，並不會久留於此，不致產生危害。」
      p 「要是我不去呢？」玉輝冷笑道。
      p 「大人會想去的，」彌爾道，「尤其當大人知道冥尊願意提供五萬大軍，助大人拿下諾薩里亞之後。」
