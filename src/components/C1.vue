      p 「這裡有點奇怪！」安琪突然叫道，「有個凹陷。」
      p 「妳站的地方是圓形的中心吧？」米庫斯道，「我想那兒便是某個入口了。彼德，你好了嗎？」
      p 「等等...好了！」彼德喊道，「我看看...哇！沒有草的地方.....一個圓，裡面是星形...」
      p 「星形？」米庫斯道，「我就知道！五角的還是六角的？」
