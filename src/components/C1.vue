      p 「哦...不是，」彼德思考了一下，說道，「我是從森林裡來的。」
      p 「森林？」米庫斯疑惑地問道，「這附近的森林裡還有別的村落嗎？」
      p 糟了，彼德心道，臨走前長老特別交代不能洩露庭苑的存在。
      p 「哦...」他思考了一下，說道，「沒有啊。我是說，森林裡沒有村落也可以住人嘛。」
      p 「所以你是蠻人囉？」安琪笑道，「不像啊。」
      p 「你怎麼知道？」彼德唬道，「我就是蠻人。」其實他根本不清楚「蠻人」是什麼意思。
      p 啦哩咧的，米庫斯暗罵，說謊不打草稿。
      p 「哦...」安琪故作認真狀，心底暗笑。她當然明白這不是真的，不過也不想再追問了。
      p 「欸.....我想起來了，」彼德突然說道，「妳是不是有一個玉鐲？」
      p 「玉鐲？」安琪驚道，「你怎麼會知道？」那玉鐲正是她因瘟疫而亡的母親留給她的遺物，她最珍貴的東西。
      p 「昨晚我作了一個夢，夢裡有兩個人，就和你們一樣，」彼德道，「一個有玉鐲，一個有指環，加上我的項鍊，就一起發出了白光...然後就忘記了。」他搔了搔腦袋，卻怎麼也想不起來。
      p 米庫斯十分驚訝。他的指環是祖父在他到神廟之前，送給他的信物，從來沒有配載過。當年父親還意味深長地對他說：「要持著它，活著，直到將它傳給你的子孫。」
      p 他從沒告訴任何人，包括安琪。這個冒牌野蠻人怎麼會知道？
      h3 3.1.3.鐘墨芝石的指引
