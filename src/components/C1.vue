      p 「怎麼會這樣？」她驚訝地說道，「這周圍...簡直像幻境一般！」
      p 「會不會有怪物？」彼德警覺地說道，隨即從肩上抽出弓，將箭搭上。
      p 「等等...」米庫斯道，「有點怪...大部份的地方都長滿了芒草，但是有些地方卻寸草不生。」
