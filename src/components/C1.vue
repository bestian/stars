      p 彼德往回走了幾步，突然叫道：「有鬼！回不去了！」
      p 「什麼？」安琪道。
      p 「被擋住了，像是無形的牆，」彼德驚恐地說道，「我們被耍了！」
      p 「別這麼大聲嚷嚷，呆子，」米庫斯道，「這不過是一個結界罷啦。我正在找它的規律...」
