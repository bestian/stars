      h3 2.7.1.鎗騎團的挑戰
      p 洛南紀五十九年十一月二十六日，沃土
      p 「報告大人，」一名士兵道，「前線的夥伴遇到了困難。」
      p 「什麼困難？」聖鎗騎士英格麗絲在帳中說道。她現在是討伐北區盜匪的司令。
      p 「發生了奇怪的事情。」士兵道。
      p 「什麼奇怪的事情？」英格麗絲道，同時示意旁邊的芬斯特藍留神。
      p 「我們照原定計畫，開向馬鞍丘東側，朝著那裡的強盜山寨。原本一切順利，直到一陣巨大的冰風暴出現為止。」
      p 「冬季偶而會有這種風暴的。」騎士道。
      p 「不，大人，」士兵著急地說道，「那不是普通的風暴。在風暴中出現了許多的白骨戰士，他們空洞的眼神和邪惡的笑容，加上銳利的刀劍，讓我們幾乎要嚇破膽。
      p 「艾斯大人下令徹退，但我們徹退時又受到骷髏射手的伏擊，我們射向牠們的箭卻都穿透牠們空洞的骨架了。我敢說，用弓箭對付牠們不是個好主意。
      p 「結果我們七十位夥伴殞難，又折損了十五匹戰馬。克勞迪小隊長和費爾小隊長都殉職了。艾斯大人受了箭創，中了劇毒，現在由傑爾德大人負責治療，已無大礙，但短期內無法出戰。
      p 「我們損傷慘重，恐怕馬鞍丘的盜匪們會乘機報復。況且……他們有了這些可怕的幫兇。」
      p 「這些賊子……」英格麗絲恨恨地道，「我得給他們一點教訓才行……」
      p 「等等，」芬斯特藍插口道，「妳清楚他們的底細嗎？」
      p 「當然！」騎士道，「他們並不只是馬鞍丘的那些小賊，而是邪惡的考古學家！他們濫用魔法的奧秘，製造出汙穢的活屍，我們必須擊潰他們！」
      p 「怎麼做？」芬斯特藍憂愁地問道，「我們有足夠的力量嗎？況且對方佔了地利之便。」
      p 「我們必須先發動一波突擊，目的在探出對方的底細，之後才能規劃下一步。馬鞍丘一破，附近的盜賊勢力必然迅速瓦解。走吧，事不宜遲！」英格麗絲道。
      p 芬斯特藍原本在和她討論攻下馬鞍丘之後的戰略，沒想到在馬鞍丘就碰到了大麻煩。
      p 「好吧，」他道，「我去準備。」
      p 不多時集結的號角聲響徹了全營。士兵們正吃完午飯，現在都趕忙打點好武裝，出來列隊。
      p 英格麗絲挑選了最為驍勇的幾名騎士：聖槍傑爾德、戰士歐利夫、戰士刀疤、參謀芬斯特藍和她自己做為領隊，每隊各帶四十名備有長槍和弓劍的騎兵，開往馬鞍丘東側。
      p 他們的縱隊由前往後是：英格麗絲隊、傑爾德隊、芬斯特藍隊、歐利夫隊和刀疤隊。
      p 一小時不到他們就到了山腳，但原本住紮的哨隊已消失得無影無蹤。
      p 「該死！夥伴們都……」英格麗絲沉默了一陣，才說道，「我們兵分三路。傑爾德兄，你和芬斯特藍走正南面的山道；刀疤，你和歐利夫走東側；我由馬鞍丘雙峰間上到崚線，再由西往前。」
      p 「沒有人敢騎馬走那條路的，」聖鎗團的傑爾德好意提醒道，「那條路充斥著懸崖和陡坡。」
      p 「常言道：出其不意，攻其不備。」英格麗絲道，「放心吧，我會小心行事的。別忘了，若遇到施法者，先集中全力打倒他先說。」
      p 於是他們便兵分三路，開往馬鞍丘。
