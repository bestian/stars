      p 「受死吧，烏風！」他再度背刺。
      p 他沒有擊中，烏風閃開了。
      p 「怎麼可能？」褐刺驚道。
      p 他看到烏風轉身瞪視著他，而腦中的雜音愈來愈強。
      p 褐刺想停止讀心術的繫結，但已經來不及了。
      p 呵呵呵，哈哈哈，呵呵哈哈哈──
