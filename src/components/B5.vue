      h3 2.5.6.瓦林娜的突破
      p 洛南紀十一月十八日，沃土，右首戰場
      p 瓦林娜明白，這是法師對決，關鍵在於成功施展的進階法術，以及中斷別人施法。
      p 由於進階法術的威力較大，施法的時間通常略久一些，這時只要中到一記傷害性的初階法術，心神一亂，施法就必定失敗了。
      p 法師們一天所記憶的法術是很有限的，特別是進階法術。因此，只要成功中斷對方的進階法術，或讓對方中到自己的進階法術，便可以致勝。
      p 在聽到決鬥開始時，她與對方不約而同地取出腰間的短杖，揮了過去。
      p 四枚魔彈以迅雷不及掩耳的速度交錯而過，分別擊中了兩人。
      p 瓦林娜晃了一下，受了些傷；而那人似乎一點感覺都沒有。
      p 瓦林娜唸道：「Vil-za-vee」
      p 法力聚集成一道綠色的酸箭，向那人射去，卻在剛要擊中時化為烏有。而那人已唸出咒文：「Ris-Quase-fill」而變出了五個一模一樣的幻象，讓瓦林娜分不清哪一個才是她的目標。
      p 瓦林娜決定放手一搏，使出奧義「Pras-Tika-Nusa」橫著劈出兩道風切，卻沒有造成任何效果。
      p 對方此時已唸出了魔彈術：「Via-Nasee」而他的法力遠強過瓦林娜在諾薩里亞遇到的對手，一句咒語能射出四枚魔彈。
      p 紫色的光球重重擊上她身上。她嘔出一口鮮血，自知再也抵受不了任何一擊。
      p 她在對方施出下一個攻擊之前，趕緊唸道：「Reel-Quase-oil」
      p 她隱形了。對方的魔彈落空，射進了雪堆裡。
      p 「哈哈哈！憑這點小把戲就想躲過我的追擊？」那人大笑道，「妳太天真了！」
      p 他唸出咒文：「Vil-Kon-muta」
      p 一片閃亮的塵土自杖尖噴射而出，將瓦林娜和周圍的土地全都蓋滿。她雖已隱形，但由於沾滿了光塵，輪廓變得十分明顯。
      p 難道就這樣結束了嗎？瓦林娜心道，對方防護咒語的能力，似乎是師父一再警告過的『小型無敵護球』，再加上鏡影術的保護，法術和武器都傷不了他，而自己已完全暴露於敵方的攻擊之下，再無招架之力。
      p 不，她心道，我太依賴法術了。
      p 對方唸出一擊必殺的進階法術：「Pras-」
      p 突然他被眼中飛進的白雪打斷了施法。
      p 「怎麼會─」那人完全沒料到自己竟然會中招。
      p 瓦林娜成功地用掃腿逃過一劫，並暫時阻住了對方的視線。
      p 她迅即補上一記風切：「Pras-Tika-Nusa」卻是切在那人前方的雪地，掃起了大片的白雪，將那人蓋得滿頭滿身都是。
      p 瓦林娜趁那人忙著拍掉眼上的雪時，丟掉法杖，掏出了護身匕首，向前直衝。
      p 她毫不理會鏡影術造成的幻象，而直盯著雪地裡足跡所在之處，飛撲向前。
      p 一聲慘叫傳出，那人隨即舉手投降。
      p 你投降得太早了，瓦林娜心道，因為你太依賴法術的保護了。
      p 「勝！」她喊道。
      p 「負！」那人不情願地喊道。
      h3 2.5.7.烏風與影法師
      p 洛南紀十一月十八日，沃土，左首戰場
      p 他不是法師，烏風心道，而是殺手。既然這是殺手間的對決，那就是比真功夫了。就算不靠割喉刀和接鏢手套，對方也絕無勝算。
      p 一聽到決鬥開始，立即射出割喉刀。
      p 那人俐落地閃掉了，隨即唸出咒語：「Ris-Res-vi-la」
      p 烏風突然眼前一黑，失去了視覺。對方竟會施法！
      p 「人有五種感覺，視覺、聽覺、嗅覺、味覺、和觸覺，戰鬥用的只有前面兩種，視和聽。」聲音說。
      p 烏風一面向聲音出處發射割喉刀，一面又聽到聲音繼續說道：「現在你已失了視覺。再失去聽覺，你就完了。這些剝奪感官的法術都是永久的，會讓你一輩子成為廢人，烏風。」
      p 烏風一語不發，只是尋著聲音的來源，不斷擲出割喉刀。
      p 「不過，」那人又道，「在你失去聽覺前，我會讓你知道你是死在誰的手下。我是影部的。」
      p 「影部？」烏風驚道，「公會怎麼...」
      p 烏風想起「朔時天旨降，望時身形藏，影俱千變化，圓放大光芒」這首月影公會人人皆知的短詩，其中「影俱千變化」是指影部由施法者組成。他們是公會裡的咒術士和死冥術士，精於降咒、操屍。
      p 「我們影部奉黑主之命，已與寒冰會同盟。」那人冷笑道，「而他們提供的交換條件之一，就是你這叛徒的行蹤。」
      p 烏風聽到對方一面說話，一面閃避，呼吸絲毫沒有急促，似乎遊刃有餘，不禁暗暗心驚。戰鬥一開始，就被下了盲咒，該怎麼反敗為勝呢？
      p 「對了，再告訴你，我是影部的副堂主。」對方冷笑道，「快投降，留你個全屍。」
      p 「副堂主...影法師褐剌！原來是你！」烏風驚叫。
      p 他瞭解自已的對手有多恐怖了。
      p 影法師、戰法師和聖法師是極為罕見的法師，是除施法外，兼具其他專長的人。人們偶而會將威猛擅戰的法師稱為戰法師，但若要得到影法師的稱號，一定是兼具盜賊和殺手的技藝。
      p 褐刺不僅是影法師，還能升到副堂主，代表他的武藝和法力都不容小覷。他和烏風同樣是月影公會的後起之秀，也曾和烏風一起出過任務。烏風知道，褐剌有慎密的心思，以及無比的殘酷。
      p 烏風雙手各扣兩柄飛刀，大喝一聲，同時擲出。
      p 雖然四柄飛刀極難閃躲，但烏風還是沒聽到任何悶哼或是慘叫。
      p 烏風直撲向前，決定以近身戰決勝負。
      p 「讓你打個夠吧。」只聽得褐刺的聲音冷冷地傳來。
      p 烏風感覺到已經近身，便以飛快的速度連續刺出。
      p 「左，右，左，左...」褐刺的聲音就在身邊傳來。他完全沒有中招，也沒有反擊。
      p 烏風感到無比的恐懼，他不禁以為自己的每一個動作都被事先預料到了。
      p 他不斷地出招，落空，出招，落空...
      p 褐刺唸出咒語：「Ris-Res-vi-lio」
      p 沒有產生效果。烏風體內的闇精靈血液讓他抗住了魔法的力量。
      p 褐刺察覺到了，便一面繼續閃躲，一面再次唸道：「Ris-Res-vi-lio」
      p 烏風突然聽不見自己和對方的呼吸聲、腳步聲和其他熟悉的聲音了。他感到置身於虛空中，似乎四面八方都會有敵人來襲。
      p 烏風靜靜地站著，感受周圍空氣的流動。
      p 一把短劍從後背刺中了他。若不是劍尖一及身便旋身側閃，他已經被洞穿了。他隨即反擊一刀，卻什麼也沒砍中。
      p 冷靜，冷靜，他告訴自己，一定有辦法。
      p 當然有辦法。
      p 是夢中的聲音？怎麼會？
      p 你的心已被看透。
      p 是魔法，對吧？
      p 沒錯。只是讀心術罷了。
      p 這時，他又中了一記背刺，他覺得異常痛苦，似乎劍上塗有劇毒。他還擊一刀，還是落空。
      p 讓我來幫你。
      p 褐刺使用讀心術，並接連降咒，讓烏風目盲、耳聾，原已穩居不敗之地，但在第二下背刺之後突然感覺到了雜音。那是一個微弱而黑暗的聲音，一個毒蛇般的聲音。
