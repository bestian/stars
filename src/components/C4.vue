      p 「呃！」修士的喉頭被一刀剌穿，瞬間倒地。活屍們失去了控制，呆在原地。
      p 烏風趁機衝出包圍，直奔那名法師。
      p 那法師用冰冷的語調唸出咒文：「Vil-Ris-Kuva」並揮動著法杖。
      p 杖尖噴射出一片粘膩的魔網，將烏風連同周圍一大片沙土緊緊綑住。
      p 「哼哼，不自量力的傢伙，待會再來收拾你。」法師說著走近他的同伴，確定他是否有救。不過嗜血割喉刀已將那修士的生命帶走了。
