      h3 2.3.2. 重建「海之擁」

      p 洛南紀五十九年十一月七日，王都諾薩里亞

      p 「馬法達在上，這就是諾薩里亞啊！」安琪望著高大的城牆和飄揚的金龍旗，不禁讚嘆道。

      p 「不錯，」米庫斯道，「這兒可是全世界最繁華的城市喔！洛雲城跟它比起來都差一大截，更別提老家那兒的市鎮啦。」

      p 「不過，說真的，能平安到達這裡，都得感謝安琪，」卡穆里道，「她的醫術救了我們兩個。」

