      p 「好啦，」卡穆里道，「是該道別的時候了。我要先到我叔叔那兒待一段時間，等腳傷好了，再來重新找個地方開旅店，或許就在諾薩里亞吧。」

      p 「你還會沿用原來的店名嗎？」米庫道問道。

      p 「我想會吧，畢竟海之擁就是我的生命。那是我年輕時一手創立的，當時的合夥人也在瘟疫中喪生了。」

