      p 「有什麼不好？」米庫斯道，「有混亂才有進步啊。如果不是戰國的亂像，怎麼會有洛南的盛世呢？」

      p 「可惜啊，老弟，」卡穆里道，「盛世對你來說將只是童年的回憶。」

      p 「不，只是傳奇罷了，」米庫斯道，「我的童年並不愉快。一點也不...」他的眼神露出一抹哀傷，迅即又恢復原狀。

      p 「嗯，」安琪道，「我還依稀記得去神廟前的日子...你的確不受舅舅和舅媽疼愛。他們把你當成...」

