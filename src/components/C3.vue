      h3 2.3.3. 採購

      p 「對了，你剛說到你叔叔。他在這裡作什麼營生呢？」米庫斯向『海之擁』老闆卡穆里問道。

      p 「畫師，」卡穆里道，「在王都，『哲拉大師』可有名了。雖然年紀大了，畫是愈來愈好。對了，你們在諾薩里亞的這段期間，或許可以住他那兒，我想他不會反對的。」

