      p 「什麼嘛！」安琪慍道，「虧我好心照顧你那麼久。你自己說，在對抗詛咒的那整整十天裡，我有沒有離開過你？」

      p 「我怎麼知道，我在專心對付詛咒啊！」米庫斯賴皮地笑道。

      p 「哼，小心下次我不救你了。」安琪慍道。

      p 「那妳再被關到牢裡時，我也不救妳啦。」米庫斯笑道。

      p 在他們伴嘴的同時，馬夫艾瑞爾已載著他們通過守衛，通過皇都高聳的城門了。

      p 眼前是他們從沒見過的繁華大道。一棟棟乳白色的建築物聳立兩旁，是用高級的大理石砌成。街上充斥著小販和行人，熱鬧繁華到讓人覺得自已渺小。

      p 打扮的花枝招展的貴婦人們，正由僕人陪著走向廣場，聆聽吟遊詩人的演唱；替師傅跑腿的年輕學徒充滿朝氣地衝向市集，尋找連自己都不明白用途的東西。

