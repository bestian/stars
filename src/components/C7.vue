      h3 3.7.6. 潛入月影
      p 洛南紀六十年一月十七日，北城
      p 烏風輕易地在一棟空屋內找到地道，兩人便躲了進去。
      p 「密語。」前方一個聲音道。
      p 瓦林娜悄聲唸了一個咒語。
      p 「我們不是已經說過了嗎？」她用著具有魔力的聲音說道。
      p 「你們已經說過了？...」蒙著面的守衛道，「喔，對...那你們下去吧。下一層的密語是『銀狐月光』。」
      p 兩人收掉神球的隱身結界，走下一級級蜿蜒的地道。
      p 「密語。」前方又有一聲音說道。
      p 「『銀狐月光』。」瓦林娜道。
      p 「請隨我來。」黑衣人點起火把，將他們帶到一扇青銅門邊。
      p 月影的守衛把門推開，裡面坐著六名黑衣人。房間的後面還有兩道門，不知通往何處。
