      h3 3.7.4. 脫身
      p 洛南紀六十年一月十七日，北城
      p 黑影撲下，一把刀剌進紫焰的咽喉。她完全來不及閃躲。
      p 黑衣人在地上打了個滾，起身看著自己的勝利。沒有人被割喉還能活著的。
      p 突然一把冰箭，夾著一股寒氣向他射來。
      p 烏風一把接住箭，訝異地望著正張弓搭上第二枝箭的星使。
      p 紫焰面露兇光，眼神透出冰冷的殺意。雖然血液不斷自喉嚨湧出，但星使卻沒有倒下。
      p 紫焰射出第二箭，說道，「你擊中我袍子的罩門，很不簡單。但我不是凡人比得了的...」
      p 烏風不加思索，再接住箭，準備閃身遁走，突然冰箭在手中爆炸，裡面一團極冷的凍氣擊中烏風。
      p 他咬牙忍住，扺抗凍氣，用護身法力，讓自己免於重傷。闇精靈的血液再次救了他。
