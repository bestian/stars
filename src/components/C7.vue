      h3 3.7.1. 寶劍與神球
      p 洛南紀六十年一月十七日，神殿廢墟
      p 「終於到了！」彼德道，「萬歲！」他完全無視於視野所及內的十數幾怪物，開心地叫道。
      p 「嗯，」安琪道，「看這祭壇，簡直像新的！」
      p 「別說祭壇，」米庫斯道，「就連柱子，雖然古老，但卻堆的異常整齊，不是嗎？」
      p 「喂，你們看！」彼德道，「那隻蜥蝪怪在做什麼？」
      p 「我看看，哇噢...」米庫斯道，「耐頓在上，牠在搬石塊！堆神殿用的石塊！」
      p 「不會吧？」安琪道，「牠們在重建神殿？」
      p 「我有不好的預感，」米庫斯道，「怪物在重建神殿，恐怕這裡的神不是善神。」
      p 「一定是邪神。」安琪道，「不過還是去祭壇前看看聖徽好了。」
      p 兩人走近祭壇，四處搜尋代表神祇的聖徽。
      p 找了一陣，米庫斯叫道：「盤蛇聖徽！是痛惡之神！」
      p 痛惡之神奧巴音是中立邪惡的神，代表純粹的恨意與怒火。在九主神中，平衡之神瑪法達、審判之神歐法拉、災禍之神魔力塔是和他相鄰的。
      p 「還好不是審判之神！」安琪道，「雖然我不喜歡痛惡之神奧巴音，但至少我的主神和祂有交情。」
      p 米庫斯道：「但是，這座神殿沒有住持，有什麼用呢？」
      p 安琪道：「住持一定很快會出現，你看怪物們已經把神殿快蓋好了。」
      p 「我們也去幫忙蓋吧！」彼德高興地說道。
      p 於是三人加入怪物的行列，堆著堆著，突然風雲變換，豆粒大的雨點一滴滴打了下來。
      p 「雖然怪物不怕雨，但是我們還是快躲起來吧！」米庫斯道。
      p 「我可不想讓酒被稀釋！」彼德叫道。
      p 三人向最近的一處石壁奔去，尋找內凹可以躲雨的地方。
      p 找著找著，忽著發現一處陰影後面，竟有一個石洞！
      p 彼德點亮火把，率先走了進去。
      p 「好險沒濕得太厲害！」安琪道，一面甩開頭髮上的雨水。
      p 「呼！好在裡面這麼寬敞。」米庫斯大鬆一口氣，坐了下來。
      p 忽然一股脆異的光線使他抬起頭。
      p 「哦，不！」米庫斯驚訝地叫道。
      p 「怎麼了？」彼德道。
      p 他看見米庫斯伸手指向洞穴的高處，說不出話。
      p 順著他的手指往上看，只見天花板上，嵌著一塊黑色的寶石，有頭顱般大小，上面帶著暗紅色的斑痕，卻發出綠色的熒光。
      p 「瑪法達在上，又是暗血石！」安琪叫道。
      p 這顆暗血石比上次那顆大了十倍還不止，光芒更是亮得嚇人。
      p 「這裡不是普通的地方，我要找找看...」米庫斯站起身，四處搜索暗門和密道。
      p 「找到了，在這裡...一扇青銅門...可能是愛森時代的產物，」米庫斯在洞的深處說道，「接著就是開鎖、解陷井...我的工具盒呢？竟然忘了帶！」
      p 他回到洞穴中央，從背袋中拿出一只小盒，又走了進去。
      p 「我們跟上吧，」安琪道，「怕會有什麼東西衝出來。」
      p 洞穴深處傳開卡嗒、卡嗒的聲音。
      p 「快好了，」米庫斯道，「這鎖還滿好撬...等等！」
      p 他注意到鎖孔內一小塊奇怪的機關。
      p 奇怪...發射孔在哪？
      p 他環視四周，發現牆上滿是針孔般的小洞，不仔細看，會認會只是岩穴天然的孔洞而己。
      p 好個陷阱！米庫斯心道，若我剛才急著撬鎖，現在已被萬箭穿身了！
      p 長期研究精工、開鎖與機關，米庫斯對各式陷井十分熟悉，也十分小心。如果有機會，他也很想一展設陷井的長才，但這次旅程都在不停行進，沒有讓他設陷阱的機會。
      p 機關、陷井的功能各式各樣，像是警報、捕捉、傷害、至人於死。如果有法師協助，還可以附有強大的魔法。
      p 在米庫斯過去的冒險中，也曾被毒箭擦過、掉入陷坑。即便是技術老練的竊賊，也不想因大意而冒不必要的險，白白送命。
      p 他沈思了一會兒，取出細線、小鐵條和滑輪製作了一個機關，可以從遠端將撬鎖器拉入鎖孔。
      p 他退了三步，舉手示意彼德和安琪也後退，再小心翼翼地拉動細線。
      p 沙沙沙一陣聲響，許多細針從牆上射出，釘入對面的牆上。
      p 彼德與安琪從未見過這樣狠毒精巧的機關，不禁駭然。
      p 米庫斯準備向前，突然想起剛才在門邊有看到人骨。於是他等了一陣，再次拉動細線。
      p 如他所料，又是一陣針雨。
      p 米庫斯的謹慎救了他的命。這是愛森時代遺留下來的寶窟，雖然隱密，難保不會遭遇多次入侵。當初設計者早料到機關不會只用一次，所以準備了非常多的細針，以及重覆啟動的機關。
      p 米庫斯花了將近十分鐘，重覆等待、拉動的動作，直到細針不再射出，還不放心，又拉了幾次。確定安全後，才走近青銅門，將它撬開。
      p 洞內滿是符文，角落有十幾瓶藥劑，有的可能因為沒蓋緊，已經揮發掉了，只留下或紅或藍的痕跡。最顯眼的，是一把平放在石桌上的青銅長劍，和旁邊的一顆刻滿法印的青銅球。
      p 米庫斯取出小木棍，四處敲打試探，尋找暗門和陷井。
      p 安琪和彼德跟進，訝異於牆上複雜的樣式。
      p 彼德看到桌上的劍，高興地走向前，拭去上面的塵土，將它拿了起來。
      p 「等等！」米庫斯叫道。但已來不及了。
      p 彼德拿起劍，疑惑地看著米庫斯：「怎麼了嗎？」
      p 「那上面...你這個笨蛋！」米庫斯怒道。
      p 彼德看看劍，又看看他，一臉茫然。
      p 「那上面可能有機關或陷井法術！」安琪好心地說道。
      p 「可是沒有啊。」彼德很無辜。
      p 「啦哩咧的，沒遇到是你好狗運，」米庫斯道，「光是一個『可能』就會讓你死一萬次也不夠，還得把同伴的命也陪上。」
      p 「哦...」彼德道，「那我下次注意。」
      p 米庫斯憤憤地繼續搜索，在一面牆後找到了一扇小的暗門，大約只夠放一件東西，人是進不去的。
      p 他仔細地搜索可能的陷井，又唸出禱文：
      p 「浪濤之神耐頓！顯現隱藏的未知！賜我以神祐之眼，望穿聖力與魔術的痕跡。」
      p 唸完禱文，他發現暗門上沒有任何魔法的力量，但青銅球和彼德手中的青銅劍上，都發出了難以置信的強光。尤其是那只比手掌略大的青銅球，光芒竟無比耀眼，令人難以直視。
      p 米庫斯嚇呆了。在偵測魔法的禱文下，連暗血石的光芒都遠遠不及這顆球。太詭異，太強大了！
      p 神定之後，他打開暗門，發現裡面只有一片青銅板，上面刻著古老的文字和符印。
      p 米庫斯將它取出，又查了查暗櫃裡是不是還有暗櫃。確定沒有後，他轉身走向青銅球。
      p 彼德正輕輕地舞著青銅劍。青銅質武器比起鐵製的是劣質品，但這把劍卻很不同。輕巧，鋒利，比鐵劍有過之而無不及。若不是彼德腰間的佩劍是從受訓開始就陪他到現在，數年的夥伴，他必然會直接將劍換掉。
      p 不過，現在彼德對新劍著迷的程度，也可看出它的不凡。
      p 米庫斯稍微檢察了一下青銅球旁是否有機關、陷井。確定沒有後，他將青銅球舉起。
      p 忽然，整個山洞開始震動。
      p 原本放球之處底下的桌面出現了一小塊突起。查不到的機關！
      p 山洞震得更厲害了，石削紛紛從洞頂掉落。
      p 安琪正打包藥瓶到一半，眼看不對，快速將手中的綠色藥劑塞入背袋後一側，轉身背上，衝了出去。
      p 彼德緊跟再後，而米庫斯拿著青銅球跑在最後。
      p 三人衝出暗室。
      p 毫無預警地，一股致命的寒氣從前方襲來。
      h3 3.7.2. 紫焰和瓦林娜
      p 洛南紀六十年一月十七日，北城
      p 瓦林娜飛向『迷之鑰』旅店，走近半開的門。
      p 隱身術使她感到安全，於是她走了進去，準備尋找芬斯特藍。
      p 她進到大廳時，環視四週，沒有芬斯特藍的影子。或許是在樓上吧，她心道。
      p 「妳在找什麼？」柔和的聲音傳來。
      p 她轉頭過去，看見一名披著淡紫色袍的子長髮女子，約二十來歲，坐在角落的一張小桌旁，喝著熱湯。
      p 瓦林娜注意到她腳邊除了背袋外，還放了一把短弓。弓上刻著奇怪的花紋，十分眩目。
      p 「妳看得見我？」瓦林娜走近，輕聲問道。
      p 「嗯，」紫袍人點頭微笑，「一起坐嗎？」
      p 瓦林娜想了想，反正芬斯特藍就在此處，也不急在一時，便走過去坐在那人身旁，將法杖靠在伸手可及之處。
      p 「閣下是...」瓦林娜問道。
      p 「我叫紫焰，」紫袍人笑道，「你可以叫我紫焰姐姐。我的弟妹們都是這麼叫的。」
      p 「紫焰...」瓦林娜喃喃地說道，「好像在哪兒聽過...對了，我叫瓦林娜，是風谷的結界師。」
      p 「嗯，我知道，」紫焰笑道，「我來這兒就是為了等你的。」
      p 「哦？」瓦林娜奇道，「妳知道我要來？」
      p 她心中暗自戒備，想好各種脫身與攻擊的法術。
      p 紫焰微微點頭：「是啊，我等這天等了好久...」
      p 瓦林娜道：「妳怎麼知道我要來？」
      p 「我當然知道，」紫焰道，「小妹妹，妳會來此是命中註定...」
      p 「怎麼說？」
      p 「妳是來找芬斯特藍的，不是嗎？」
      p 「啊！」瓦林娜道，「是這樣...可是你怎麼知道？」
      p 「妳的占卜我都看在眼裡啊，」紫焰道，「影響占卜的法術啊，我也懂得不少哦...」
      p 「所以...」瓦林娜驚道，「妳是說他不在這裡？」
      p 「別急，別急，」紫焰道，「該來的總是會來的。」
      p 「什麼意思？」瓦林娜道，「妳是說我還會看到他？」
      p 「不，不，」紫焰笑道，「我是說妳再也不可能看到她了。」
      p 「什麼？」瓦林娜驚道，「妳把他怎麼了？」
      p 「不，不，」紫焰搖頭笑道，「芬斯特藍好的很，我沒有把他怎麼了。我是要把『妳』怎麼了。」
      p 雖然已想過這種可能，但瓦林娜還是大吃一驚，沒想到烏風竟有先見之明！想到烏風躲在暗處準備伏擊，她便強自鎮定下來。
      p 「妳要把我怎麼了？」她笑道。聲音中不帶一絲恐懼。
      p 紫焰道：「我要把妳帶到另一個世界，從身體中釋放。」
      p 「要取走我的靈魂嗎？妳到底是誰？」
      p 「我是紫焰，宿命星使紫焰，」星使緩緩地說道，「命運的使者，時間之弦的樂師。世界的脈動不容許輕易改變，想推翻命運安排的人終將受到報應。」
      p 「什麼是命運的安排呢？我們風谷的人從未聽過。」
      p 「你們如風般瞬息萬變，卻不知風是轉瞬即逝的。但是，每年大氣的走向，旱季和雨季都是固定的。這就是命運的安排。
      p 「你們像海面上的浪花，覺得自己特殊、自由，卻不知真正推動歷史的，是海底的潛流啊！」
      p 「既然如此，妳為何要阻止我呢？如果說我的努力都將歸於徒然...」瓦林娜反問。
      p 「因為妳有改變命運的潛力...妳是一位真正的冒險者，又流著不凡的血液...精靈族和愛森大法師的血神秘地交溶起來，使妳擁有超出凡人的潛力。不過，最重要的，還是妳那顆飛翔不羈的心。」
      p 「嗯。」瓦林娜簡單地答道。
      p 「妳的心中有一股不息的風。」紫焰道，「而今天我要來讓它止息。為了諸神的計劃。」
      p 「那就來吧。」瓦林娜簡單地答道。她信任自己，也信任烏風。
      p 星使就在她眼前觸手可及之處，散發著柔和而恐怖的氣息。
      p 四周的人們並未注意到一觸及發的戰鬥。
      p 紫焰拿起弓，突然周圍的時間似乎凝結了，慢了下來。
      p 瓦林娜看到星使的手一動，便開始唸咒，但還是慢了一步。
      p 宿命之神的使者張開弓，一隻若有若無的冰箭就憑空浮現，似乎是由空氣中的水氣凝聚成的。
      p 「接受妳的命運吧！重入輪迴之中！」紫焰喊道。冰箭在空中化為一片錐狀的凍氣，襲上瓦林娜和身後的客人、酒保。
      p 強烈的寒氣令結界師心神一分。全身和喉頭都是一陣剌痛，咒語唸到一半就卡住了，法力消散於空氣之中。
      p 被寒冰錐擊中的人都成了冰雕。
      p 瓦林娜身上雖附著抗寒的法術，但仍然冷得發抖，四肢都僵掉了，而肺更是難受。
      p 致命的寒氣讓瓦林娜幾近昏厥，但堅強的意志強迫她清醒。
      p 她拿往法杖，唸出咒文：「Pras-Bilifiligi」
      p 瓦林娜憑空消失了。
      p 紫焰愣了一下，知道對方用遁術逃走了，但這法術的距離有限，要追回來並不難。
      p 她環視四周，本來在旅店中的許多冒險者準備合力朝她攻去。
      p 紫焰一閃身從門口竄出，刀、劍、斧和箭矢從她背後略過：都慢了一步。
      h3 3.7.3. 亂舞
      p 洛南紀六十年一月十七日，神殿廢墟
      p 彼德閃開對方的攻勢，持青銅劍砍去。一個人形的黑影受傷退卻。彼德看不清他的面孔，但時間緊迫，於是跟著往反方向一閃，衝了出去。
      p 當他衝出山洞，離開暗血石光芒的籠罩後，發覺米庫斯和安琪也衝了出來，但好像帶了傷，看起來十分虛弱。
      p 山洞隨即崩塌。
      p 「呼！」彼德喜道，「不管那是什麼，好在牠再也出不來了。」
      p 米庫斯道：「希望如此...但剛才的攻擊實在怪異，我覺得身上的精力被吸走啦！」
      p 安琪道道：「活屍...會不會是影魔？」
      p 「什麼是影魔？」彼德問。
      p 「希望不是。」安琪沒有正面回答。
      p 「等等...你們看！」米庫斯叫道，「神殿蓋好了！」
      p 「那些怪物呢？」彼德問。
      p 「在那裡，祭壇旁邊...」安琪道，「牠們圍成一圈...在跳舞！」
      p 「真浪漫的怪物！」米庫斯笑道，「牠們在慶功嗎？」
      p 「不...」安琪道，「圓圈的中央...仔細看，不是有個光影嗎？好像是一個人。」
      p 「是什麼人呢？」米庫斯奇道，「牠們在召喚以前住持的亡靈嗎？」
      p 「不知道...」安琪道，「我們要冒雨過去嗎？」
      p 彼德聳聳肩。
      p 米庫斯道：「我去探探。」
      p 他潛入黃昏之中，溶進林內，悄然近前。
      p 只見祭壇旁畫了好大一個五角召喚圈，圈外是二十多隻的怪物不停地跳著舞。牠們野蠻、亂無章法的舞步，循著共同的節奏，不斷敲擊地面。
      p 怪物們眼神興奮，幾近瘋狂。
      p 三隻大飛熊在這當中尤為顯眼。牠們不停跳動，偶而吼聲幾聲，其他如螳螂怪、蜥蜴怪等小怪物便跟著應和。
