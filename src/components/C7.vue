      p 「得賭，而且別無選擇。」彼德道，「我根本不相信邪神星使會幫想除掉祂的人治病。」
      p 「好吧...」米庫斯道，「安琪，妳研究一下藥水，我研究一下這顆球，再來討論戰術。」
      p 「可是召喚快完成了...」彼德道。
      p 「現在殺過去沒有勝算啊！」米庫斯道，「就算我們有怪物味，但對方有三隻大飛熊！耐頓在上，三隻欸！」
      p 「好吧，你們慢慢研究，我先去干擾他們。」彼德道。
      p 「小心啊！」安琪道。
      p 「沒有問題，」彼德道，「森林裡的游擊戰我最拿手了，現在敵明我暗，正好打游擊！」
