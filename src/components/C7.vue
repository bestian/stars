      p 「明白，」彼德道，「還是要打。」他的聲音堅定。
      p 「為什麼？」安琪道。
      p 「星使，下凡，」彼德斷斷續續地說道，「會殺千千萬萬人。我們三人，可以救千千萬萬人。這是冒險者的本份。」
