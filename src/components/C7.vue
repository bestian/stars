      h2 #尾聲
      p 故事正行進到轉折困頓的時候。正如一首樂首，有高潮，也有泥沼。在泥沼中人們的掙扎，或許更為可貴。
      p 整個洛南帝國都已陷入泥沼。北方的戰事不停失利、東岸的海盜不斷侵襲、許多諸候國紛紛獨立，而剛平息的沃土之亂也有死灰復燃之勢。
      p 考古隊和探險家們接連著徹出西南山脈，使原本的一線希望也開始褪色。大帝被惡訊弄得不厭其煩，最後竟眼不見為淨，把早朝的次數減了一半有餘。
      p 星使一個個下凡，在北地、在東海、在北衛山脈、在沃土、在西南山脈、甚至王都附近都有星使的勢力。很明顯的，人類已不再能左右自己的命運。緊接著來的，就是一場諸星之戰--天界爭鬥以人間的流血為代價。勝利的神祇，將為人類的未來定向，或讓人類從世界中永遠消失。
      p 人的命運似乎就掌握在神的手中。
      p 冒險者們，也有各自的難關要面對。米庫斯等人的屍毒雖然痊癒，但被痛惡星使強迫加入怪物們的軍旅；瓦林娜和烏風暫棲北地月影，但紫焰的目光一日沒有轉移，他們也一日不得安寧；芬斯特藍和英格麗絲在一場大戰失利後，從沃土往西南山脈逃去；撰史者阿法利烏斯正執行著不知名的任務，試圖補起這破碎的世局。
      p 在這個時代中，人們身不由己。勇氣、希望、力量、覺察和真心都失去了。
      p 但是，即使處於逆境，還是有些人，不會放棄...

