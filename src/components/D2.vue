      p 「嗯…再往前也未必找得到。」烏風道，「等等，妳看那農舍穀倉旁的小屋…頂上有一個刻花的牌子。」
      p 「飛近一點吧…」瓦林娜道，「看到了…是一個星辰排列的輪子…應該是聖徽…以前聽谷裡的人說，宿命之神的聖徽就是一個輪子！」
