      h3 4.2.1.閃金村
      p 洛南紀六十年一月三十日，閃金村
      p 閃金村曾是洛南帝國沒落時的希望之鄉。
      p 現在金子淘完了、考古隊也走的差不多了，剩下的人都是腳踏實地的村民。
      p 彼德見到農人、小販、獵戶等人，以及東一撮西一撮的孩子，不禁想起隱者庭苑。
      p 隱者庭苑說近很近，就在西南山脈、盛恩村以南兩三天路程；但說遠也很遠，因為不能讓外人知道的規矩，使彼德只要還跟米庫斯和安琪冒險一天，便一天不能回去。
      p 三人雖然急於通報痛惡星使入侵的消息，以及尋找能解決影魔附身的人，但走近村子後，在一股歡樂的氣氛下，步調便也慢了下來。
      p 其實這還不是閃金村最歡樂的日子。最近怪物出沒，為村子蒙上層陰影。警備隊為此擴大編制、加緊訓練，但每當蜥蝪怪、大飛熊出沒之時，總是會帶走不少人命。
      p 彼德走在路上，一群六、七歲大的小孩跑向前來。東問西問，把彼德纏得暈頭轉向，一句話也聽不清楚。
      p 有一個小女孩大膽地伸手，想拔彼德腰間的佩劍。
      p 彼德按住她手，低聲道：「不行！」
      p 突然，彼德兩隻大手將小女孩托起，轉了三個圈，向空中一拋，再接住，親了她臉頰一下，放下地來。
      p 那小孩驚魂未定，眨眨眼，跑了。同伴們見了彼德的威勢，也一哄而散。
      p 米庫斯哈哈大笑，安琪也不禁莞薾。
      p 三人再往前走，打聽警備隊所在。村人指向村中向的一棟小屋。
      p 米庫斯走在前面，先去和門口的守衛通報道：「我們三人剛逃離魔掌，有要事向隊長稟報，是關於林中怪物的。」
      p 一聽到「林中怪物」四字，守衛馬上臉上變色。
      p 「好，我去通報。」守衛說道，隨即轉身進門。
      p 米庫斯看著守衛的背影，以及上身的皮甲，想到這村子的資源如此不足，連守衛都只有皮甲可穿，實在抵擋不住星使和怪物大舉入侵。
      p 不多時，走出來一位高大的戰士，只佩了一把長劍，沒有穿載甲胃。這人約三十歲上下，臉上輪廓清楚、線條分明，肌肉結實、眼神凝歛，看得出是有經驗的冒險者。
      p 「有什麼事？」隊長有力地說道。
      p 「是這樣的…」米庫斯將神殿廢墟、星使和怪物大舉入侵的事告知隊長。
      p 隊長的臉色愈聽愈沈重。
      p 「你們如何得知的？」他嚴肅地問。
      p 「我們剛從星使處逃脫…」米庫斯大至講了一下逃脫的經過，又問：「這兒有會驅邪的修士嗎？我們被影魔附身了。」
      p 「沒有，」隊長道，「村裡沒有。不過往西不遠處有一座冒險者的營地，最近人變少了，但還是住著不少能人異士。你們可以去看看。」
      p 「為什麼他們不在村中紮營呢？順帶協助守備。」安琪問道。
      p 「這個嘛…老實說，」隊長壓低了聲音，「他們多半是考古隊的，為人不大正派…」
      p 「怎麼說？」
      p 隊長搖了搖頭：「一言難盡。總之，和他們打交道要多小心。」
      p 米庫斯覺得好奇，但也不便多問。
      p 「多謝指點啦，」米庫斯道，「那麼關於星使和怪物入侵之事，你打算怎麼解決呢？」
      p 「照你們的說法，那些怪物少說有數百隻，」隊長憂愁地說道，「加上不朽的星使，我們小小一個閃金村，是絕對擋不了的。就算探險家營地的能人異士幫助我們，要和神對抗…恐怕還是沒什麼勝算。」
      p 隊長老到的經驗告訴他，眼前的危險是前未所見的恐怖。不能意氣用事，白白送了一村老小的性命。
      p 「所以呢？」安琪關心地問。
      p 「遷村。」隊長說道。
      p 三人愣了一下。
      p 「嗯，看來這也是唯一的辦法了。」米庫斯道。
      p 「我馬上要處理此事，不奉陪了。」隊長道，「感謝三位的報信。」
