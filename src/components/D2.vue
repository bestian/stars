      p 「難道你就能確定自己是清者？明者？」瓦林娜笑道。
      p 「至少我不與雞鶩爭食。」烏風道。回想起在月影公會，大家都等不及要晉升堂主，甚至有走後門送紅包的，不禁不屑地笑了起來。
      p 兩人飛到農舍的大門口，瓦林娜先唸出友善術的咒文：「Vil-Fre-Na-il」再落下地來，離開隱匿之球的保護範圍，現出身形。
      p 「請問…有人在嗎？」她客氣地問道，並準備好施展魅惑術。
      p 木門開啟，一名女子探出頭來：「有什麼事嗎？」
      p 她的外表和一般村婦並無兩樣，二三十歲年紀，簡單的布衣長裙、載個頭巾。不過，瓦林娜注意到她脖子上掛著一個銅質項練，底部刻著輪子形狀的聖徽。
