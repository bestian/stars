      p 夕陽斜斜地投映在豐足之原大片的田野上，紅、橙、黃、綠的色彩，染成一幅美麗的畫。
      p 但是，每十棟屋子就有九棟是殘破不全，每十片麥田就有九片是荒草叢生。
      p 戰禍、瘟疫、蝗害，使洛南人民的處境危如累卵。以農為本的洛南帝國，正在瓦解。
